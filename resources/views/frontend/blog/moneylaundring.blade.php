                    تبصره 3 - همه اشخاص مشمول مكلفند در تعاملات كاري خود با آن دسته از اشخاص مشمولي كه در رتبه ‌بندي موضوع اين ماده ضعيف ارزيابي شده ‌اند، تدابير سختگيرانه ‌تري اعمال كنند و اين اشخاص را اشخاص با خطر (ريسك) بالا در نظر بگيرند.
