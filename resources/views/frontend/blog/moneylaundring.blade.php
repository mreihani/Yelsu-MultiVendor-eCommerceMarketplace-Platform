                    تبصره ۱ - مركز مكلف است ظرف يك سال پس از تصويب اين آيين ‌نامه، روش ارزيابي و رتبه ‌بندي موضوع اين ماده را با همكاري دستگاه‌ هاي متولي نظارت در هر حوزه تعيين كند و در اختيار اشخاص مشمول قرار دهد.
