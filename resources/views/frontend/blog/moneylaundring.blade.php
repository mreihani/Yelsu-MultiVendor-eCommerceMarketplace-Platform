                </p>
                <p class="mx-auto">
                    ماده 41 – دستگاه ‌هاي متولي نظارت مكلفند با هماهنگي با مركز به‌ منظور ايجاد نظام نظارتي جامع و كارآمد، ظرف شش ماه پس از تصويب اين آيين ‌نامه، رويه‌ هاي مناسب و سامانه‌ هاي لازم را به ‌منظور پايش مستمر، كشف تخلف، صدور اخطارهاي لازم و اعمال مجازات ‌هاي متناسب، مؤثر و بازدارنده، جهت رفع نواقص و ناكارآمدي‌ ها در حوزه تحت نظارت خود طراحي و اجرا كنند.
