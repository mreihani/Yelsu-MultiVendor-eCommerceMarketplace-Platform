                <p class="mx-auto">
                    تبصره - اشخاص مشمول مكلفند از شماره تلفن همراه ثبت ‌شده براي شناسايي ارباب‌ رجوع و نيز برقراري ارتباط با اشخاص (حقيقي يا حقوقي/ ايراني يا خارجي) استفاده كنند.
                </p>
                <p class="mx-auto">
                    ماده ۲۳ - وزارت صنعت، معدن و تجارت مكلف است با همكاري سازمان ثبت اسناد و املاك كشور، ظرف شش ماه پس از تصويب اين آيين ‌نامه، سامانه مكان ‌محور ثبت معاملات املاك و مستغلات كشور را به ‌گونه‌ اي پياده ‌سازي كند كه براي كليه اشخاص حقيقي و حقوقي امكان ثبت معاملات مربوط به اموال ‌غيرمنقول، اعم از اينكه داراي سند رسمي يا ‌غيررسمي باشد، فراهم گردد.
                </p>
                <p class="mx-auto">
                    تبصره - همه مالكان اموال غيرمنقول داراي اسناد غيررسمي مكلفند ظرف شش ماه پس از پياده‌ سازي سامانه، اسناد مثبته دال بر معاملات خود را در اين سامانه بارگذاري كنند.
                </p>
                <p class="mx-auto">
                    ماده ۲۴ - نيروي انتظامي جمهوري اسلامي ايران مكلف است ظرف شش ماه پس از تصويب اين آيين ‌نامه، پايگاه يكپارچه اطلاعات ورود و خروج اشخاص حقيقي از مرزهاي كشور را به‌ نحوي ساماندهي كند كه همواره امكان استعلام وضعيت حضور افراد در داخل كشور، از جمله فهرست اتباع خارجي كه مهلت قانوني حضورشان در داخل گذشته است، براي مركز و ضابطان خاص به ‌صورت روزانه فراهم باشد.
                </p>
                <p class="mx-auto">
                    ماده ۲۵ - سازمان ثبت اسناد و املاك كشور مكلف است پايگاه يكپارچه اطلاعات وكالت‌ نامه‌ هاي رسمي را ظرف يك سال به‌ نحوي ساماندهي كند كه همواره امكان استعلام برخط وضعيت آن براي اشخاص مشمول فراهم باشد.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - منظور از وضعيت وكالت ‌نامه اقلام اطلاعاتي پايه هويتي وكيل و موكل، نوع وكالت ‌نامه و مدت اعتبار آن است.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - پس از راه ‌اندازي اين پايگاه، ارايه هرگونه خدمات وكالتي جديد و ادامه ارايه خدمات پيشين به ارباب‌ رجوع توسط اشخاص مشمول منوط به ثبت وكالت ‌نامه در پايگاه مذكور خواهد بود.
                </p>
                <p class="mx-auto">
                    تبصره 3 - سازمان ثبت اسناد و املاك كشور مكلف است فهرست آن دسته از وكالت ‌نامه ‌ها را كه به ‌دلايلي به‌ جز پايان مدت اعتبار ابطال يا تعليق شده ‌اند، به ‌همراه تاريخ و علت آن، روزانه در اختيار اشخاص مشمول قرار دهد.
                </p>
                <p class="mx-auto">
                    ماده ۲۶ - وزارت تعاون، كار و رفاه اجتماعي مكلف است با همكاري دستگاه‌ هاي اجرايي، پايگاه يكپارچه اطلاعات شغلي اشخاص حقيقي اعم از ايراني و خارجي را به‌ نحوي ساماندهي كند كه همواره امكان استعلام آخرين وضعيت فعاليت شغلي آنها براي اشخاص مشمول فراهم باشد.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - امكان استعلام بايد به‌ نحوي فراهم شود كه به ‌ازاي هر شماره ملي، براي كليه رديف‌ هاي شغلي فرد، اقلام اطلاعاتي وضعيت اشتغال (اعم از مشغول‌ به ‌كار، بيكار يا بازنشسته)، رسته و عنوان شغلي، ميانگين درآمد ماهانه، تاريخ اخذ و مدت اعتبار مجوز شغلي، تاريخ انفصال از فعاليت شغلي در نتيجه استعلام مشخص گردد. ساير اقلام اطلاعات مورد نياز بنا بر اعلام مركز خواهد بود.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - سازمان تأمين اجتماعي و ساير صندوق‌ هاي مرتبط (در خصوص اطلاعات استخدامي كارگران)، سازمان امور اداري و استخدامي كشور (در خصوص كارمندان دولت)، صندوق ‌هاي بازنشستگي (در خصوص بازنشستگان)، وزارت صنعت، معدن و تجارت (در خصوص جوازهاي كسب اصناف، مجوزهاي واحدهاي صنعتي، معدني و توليدي و دارندگان كارت‌ هاي بازرگاني)، سازمان نظام مهندسي كشور (در خصوص پروانه ‌هاي اشتغال مهندسان)، سازمان نظام پزشكي جمهوري اسلامي ايران (در خصوص پروانه طبابت) و سازمان بورس و اوراق بهادار (در خصوص شناسه (كد)‌ هاي معاملاتي اشخاص) و سازمان ‌ها و شركت‌ هاي تابع ‌هر يك مكلفند اطلاعات لازم را در خصوص اجراي اين ماده به‌ صورت ماهانه در اختيار وزارت تعاون، كار و رفاه اجتماعي قرار دهند.
                </p>
                <p class="mx-auto">
                    تبصره 3 - وزارت تعاون، كار و رفاه اجتماعي مكلف است با همكاري سازمان امور مالياتي كشور روابط شغلي اشخاص را با يكديگر شناسايي كند، به ‌نحوي كه همواره امكان استعلام مرتبطين شغلي اشخاص مورد نظر براي مركز و بانك مركزي جمهوري اسلامي ايران فراهم باشد.
                </p>
                <p class="mx-auto">
                    ماده ۲۷ - سازمان امور مالياتي كشور مكلف است در بازه ‌هاي زماني سه‌ ماهه همه اظهارنامه‌ هاي مالياتي اشخاص حقيقي و حقوقي و آخرين وضعيت پرونده مالياتي مؤديان را در اختيار اشخاص مشمول قرار دهد.
                </p>
                <p class="mx-auto">
                    تبصره 1 - سازمان امور مالياتي كشور مكلف است از اطلاعات پايگاه يكپارچه اطلاعات شغلي موضوع ماده (۲۶) اين آيين نامه، در قالب بند (ث) ماده (۱۶۹) مكرر قانون ماليات ‌هاي مستقيم و ‌به ‌عنوان قراين مالياتي براي تشخيص ماليات مؤديان استفاده كند.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - سازمان امور مالياتي كشور مكلف است پايگاه اطلاعات هوشمند و استاندارد اشخاص مرتكب جرم مالياتي موضوع ماده (۲۷۴) قانون ماليات ‌هاي مستقيم و بدهكاران مالياتي را مطابق ضوابط اعلام ‌شده توسط شورا تشكيل دهد و امكان دسترسي برخط به اين فهرست را براي مركز، دستگاه متولي نظارت، ضابطان خاص قانون و نيز شركت اعتبارسنجي موضوع مواد (۵) و (۸) قانون تسهيل اعطاي تسهيلات بانكي و كاهش هزينه‌ هاي طرح و تسريع در اجراي طرح‌ هاي توليدي و افزايش منابع مالي و كارايي بانك ‌ها - مصوب 1386 - فراهم نمايد. سازمان امور مالياتي كشور مكلف است اطلاعات مربوط به فهرست اين اشخاص را روزانه به ‌روزرساني كند.
                </p>
                <p class="mx-auto">
                    تبصره ۳ - سازمان امور مالياتي كشور مكلف است اعطاي مفاصا حساب مالياتي هرگونه املاك و مستغلات را به ثبت معاملات آن در سامانه موضوع ماده (۲۳) اين آيين ‌نامه منوط كند.
                </p>
                <p class="mx-auto">
                    ماده ۲۸ - به‌ منظور سهولت دسترسي به اطلاعات ارباب‌ رجوع و احراز صحت اسناد و اطلاعات ارايه‌ شده از سوي اشخاص، دستگاه ‌هاي متولي نظارت مكلفند پيگيري ‌ها و همكاري ‌هاي لازم را به ‌منظور دسترسي اشخاص مشمول تحت نظارت به اطلاعات مورد نياز جهت اجراي مقررات اين آيين‌ نامه انجام دهند. اين موضوع نافي مسئوليت اشخاص مشمول تحت نظارت جهت انجام مقررات آيين ‌نامه نيست.
                </p>
                <p class="mx-auto">
                    ماده ۲۹ - بانك مركزي جمهوري اسلامي ايران مكلف است ظرف شش ماه پس از تصويب اين آيين ‌نامه، سامانه شناسايي روابط اشخاص را با همكاري اشخاص مشمول و مطابق با درخواست مركز تشكيل دهد و امكان استعلام را براي مركز و ساير دستگاه ‌هاي متولي نظارت فراهم كند.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - منظور از شناسايي روابط اشخاص‌، شناسايي اشخاصي است كه با يكديگر رابطه ضمانت (اعم از تسهيلات و تعهدات)، شراكت (اعم از فعاليت شغلي شراكتي و حساب مشترك)، تسهيلات و تعهدات (ضامن‌ ها)، وكالت و خويشاوندي سببي يا نسبي دارند و يا بر اساس اظهارات ارباب‌ رجوع داراي رابطه‌ هستند.
                </p>
                <p class="mx-auto">
                    تبصره 2 - اشخاص مشمول اعم از سازمان‌ هاي ثبت احوال كشور، ثبت اسناد و املاك كشور، بورس و اوراق بهادار و امور مالياتي كشور، وزارت اطلاعات و وزارت تعاون، كار و رفاه اجتماعي مكلفند اطلاعات مورد نياز بانك مركزي جمهوري اسلامي ايران را جهت اجراي اين ماده فراهم آورند.
                </p>
                <p class="mx-auto">
                    ماده ۳۰ - جامعه حسابداران رسمي ايران مكلف است امكان استعلام برخط و بدون محدوديت صورت‌ هاي مالي حسابرسي‌ شده و ضمايم، گزارش‌ هاي حسابرسي، گزارش‌ هاي بازرس قانوني و ساير گزارش‌ هاي منضم به صورت‌ هاي مالي را براي اشخاص مشمول فراهم كند.
                </p>
                <p class="mx-auto">
                    ماده ۳۱ - ‌قوهٴ قضاييه با همكاري مركز، ظرف شش ماه پس از تصويب اين آيين ‌نامه، رويه‌ ها و راهكارهاي سامانه اي (سيستمي) لازم را جهت تبادل برخط اطلاعات قضايي در خصوص اشخاص مظنون به ارتكاب پولشويي يا تأمين مالي تروريسم به‌ نحوي فراهم كند كه از طريق آن امكان ارايه درخواست مركز به مرجع قضايي مربوط جهت اخذ دستور قضايي لازم از جمله توقيف يا رفع توقيف و اطلاع‌ رساني به ضابطان قضايي و همچنين ابلاغ آراي صادره مرجع قضايي اعم از حكم و قرار به ‌صورت سامانه اي (سيستمي) براي مركز و اشخاص مشمول مربوط فراهم شود.
                </p>
                <p class="mx-auto">
                    ماده 32 - مقامات قضايي نتيجه بررسي خود در پرونده ‌هايي كه بر مبناي گزارش مركز به مراجع قضايي مطرح مي‌ گردند، اعم از آنكه به ‌صورت قرار يا حكم باشد، به مركز ابلاغ مي كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - تصميمات قضايي صادر شده از سوي مقامات مذكور، در تدارك آمارهاي مقتضي توسط مركز، جهت تهيه سند ملي ارزيابي خطر (ريسك)، استانداردسازي شاخص‌ هاي مورد ارزيابي در تشخيص موارد مظنون به ‌پولشويي و ‌تأمين مالي تروريسم و همچنين حسب مورد ارايه بازخورد لازم به اشخاص مشمول جهت اصلاح روند و رعايت قوانين مربوط به جرايم مذكور در دستگاه هاي زيرمجموعه خود به ‌كار گرفته خواهد شد.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - قوه قضاييه سالانه آمار كلي پرونده ‌هاي مطروحه و مختومه در حوزه مبارزه با ‌پولشويي و ‌تأمين مالي تروريسم را علاوه بر پرونده‌ هاي ارسالي توسط مركز، به شورا اعلام كند.
                </p>
                <p class="mx-auto">
                    ماده 33 - قوه قضاييه با همكاري مركز ساز و كاري فراهم كند تا احكام محكوميت قطعي صادر شده در خصوص پولشويي، مطابق با ماده (36) قانون مجازات اسلامي مصوب 1 /2 /1392، علاوه بر انتشار در يكي از روزنامه‌ هاي كثيرالانتشار، به ‌طور سامانه اي (سيستمي) توسط مركز به اشخاص مشمول قانون اعلام شود.
                </p>
                <p class="mx-auto">
                    ماده 34 - كليه نهادها و دستگاه‌ هاي موضوع اين آيين ‌نامه و نيز دستگاه ‌هاي موضوع بند (ب) ماده (7) مكرر قانون مكلفند ظرف يك سال پس از تصويب اين آيين ‌نامه، دسترسي‌ هاي لازم را به سامانه‌ هايي كه به‌ موجب اين آيين ‌نامه ايجاد مي ‌شوند و سامانه‌ هايي كه به‌ موجب بند قانوني مذكور جهت دسترسي به اطلاعات تكميلي مرتبط با معاملات و تراكنش‌ هاي مشكوك مورد نياز است براي مركز فراهم كنند، به ‌نحوي كه همواره امكان دسترسي برخط و بدون محدوديت به آخرين اطلاعات براي مركز ميسر باشد.
                </p>
                <p class="mx-auto">
                    تبصره - در خصوص سامانه‌ هاي موجود، ظرف سه ماه پس از تصويب اين آيين‌ نامه، بايد امكان دسترسي براي مركز فراهم شود.
                </p>
                <p class="mx-auto">
                    ماده 35 - ارايه كليه اطلاعاتي كه به‌ موجب اين آيين ‌نامه در اختيار اشخاص مشمول قرار مي ‌گيرد، صرفاً از بستر مركز ملي تبادل اطلاعات موضوع تبصره (۲) بند (ث) ماده (۶۷) قانون برنامه ششم توسعه و در راستاي اجراي مقررات مبارزه با پولشويي و تأمين مالي تروريسم انجام خواهد پذيرفت و اشخاص مشمول مكلفند امنيت فضاي توليد و تبادل اطلاعات را مطابق ضوابط اعلام‌ شده از سوي مركز مديريت راهبردي افتاي رياست‌ جمهوري رعايت و مجوزهاي لازم را اخذ كنند. ساير ضوابط نحوه قرار گرفتن اين اطلاعات با رعايت ملاحظاتي نظير نحوه تعيين هزينه استعلام و سطح دسترسي هر يك از اشخاص مشمول توسط مركز تعيين و توسط شورا تأييد مي ‌شود.
                </p>
                <p class="mx-auto">
                    تبصره - امكان دسترسي به اطلاعات موضوع مواد اين آيين ‌نامه و سطح دسترسي هر يك از اشخاص مشمول با توجه به نوع فعاليت و ساختار سازماني آن شخص به ‌موجب دستورالعملي خواهد بود كه ظرف سه ماه پس از تصويب اين آيين‌ نامه از سوي مركز تعيين و توسط شورا تأييد مي‌ گردد.
                </p>
                <p class="mx-auto">
                    ماده 36 - اشخاص مشمول مكلفند با توجه به ساختار سازماني، نوع فعاليت و پيچيدگي ‌هاي مترتب بر فعاليت ‌ها و همچنين خطر (ريسك‌) هاي مربوط، از راهكارهاي سامانه اي (سيستمي) كافي، جامع و كارآمد براي اجراي مفاد اين آيين ‌نامه استفاده كنند.
                </p>
                <p class="mx-auto">
                    تبصره - چنانچه اشخاص مشمول تحت نظارت بنا بر شرايط خاص خود امكان استفاده از روش ‌هاي مبتني بر فناوري اطلاعات را نداشته باشند، بايد يك روش جايگزين كارآمد انتخاب كنند و به تأييد دستگاه ‌هاي متولي نظارت مربوط برسانند.
                </p>
                <p class="mx-auto">
                    فصل چهارم - ساختار و رويه‌ هاي نظارتي
                </p>
                <p class="mx-auto">
                    ماده 37 - اشخاص مشمول مكلفند واحدي را با توجه به نوع فعاليت و ساختار سازماني خود ‌به ‌عنوان مسئول مبارزه با پولشويي و تأمين مالي تروريسم به مركز معرفي كنند. مسئول واحد مبارزه با پولشويي بايد از مديران ارشد اشخاص مشمول انتخاب شود. مركز مكلف است بر اساس اهميت واحد مذكور صلاحيت تخصصي مسئول اين واحد را بررسي كند.
                </p>
