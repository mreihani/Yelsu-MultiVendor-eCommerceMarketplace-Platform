                    تبصره ۱ – دستگاه ‌هاي متولي نظارت مكلفند با توجه به ساختار سازماني، نوع فعاليت و پيچيدگي‌ هاي مترتب بر فعاليت‌ ها در حوزه تحت نظارت، خطر (ريسك) پولشويي و تأمين مالي تروريسم را ارزيابي و اقدامات نظارتي خود را بر مبناي خطر (ريسك‌) هاي ارزيابي ‌شده طراحي و بر اساس آن عمل كنند.
