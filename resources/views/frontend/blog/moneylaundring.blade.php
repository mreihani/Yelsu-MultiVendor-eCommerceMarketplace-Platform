                    تبصره ۲ - دستگاه‌ هاي متولي نظارت مكلفند بر اساس ساز و كار اعلامي مركز در اعطاي تسهيلات، اعتبارات، امتيازات يا مجوزها به اشخاص مشمول، رتبه ‌بندي موضوع اين ماده را لحاظ كنند؛به ‌صورتي ‌كه در خصوص اشخاص مشمولي كه عملكرد آنها ضعيف ارزيابي شده است، محدوديت‌ هاي متناسبي اعمال شود.
