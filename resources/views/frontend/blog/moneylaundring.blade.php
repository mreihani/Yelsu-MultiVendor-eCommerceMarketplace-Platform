                    ماده 46 - مركز مكلف است با همكاري سازمان حسابرسي و جامعه حسابداران رسمي ايران، به طراحي و تدوين رويه‌ هاي ارزيابي حسابرسان از نحوه اجراي مقررات مبارزه با پولشويي و تأمين مالي تروريسم توسط اشخاص حقوقي و تعيين معيارهاي معاملات و عمليات مشكوك اقدام كند و اين رويه‌ ها و معيارها را هر سه سال يك‌ بار به ‌روزرساني كند.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - سازمان حسابرسي و جامعه حسابداران رسمي ايران مكلفند ضمن ابلاغ رويه ‌ها و معيارهاي موضوع اين ماده و آموزش ‌هاي لازم به حسابرسان، از حسن انجام اين رويه ‌ها و گزارش معاملات و عمليات مشكوك مطابق مقررات توسط حسابرسان در سامانه جام اطمينان حاصل كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - حسابرسان مكلفند در ارزيابي ‌هاي خود از نحوه اجراي مقررات مبارزه با پولشويي و تأمين مالي تروريسم توسط اشخاص حقوقي، نحوه تكميل برگه (فرم) ارزيابي موضوع ماده (42) آيين‌ نامه را نيز لحاظ كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۳ - جامعه حسابداران رسمي ايران مكلف است بر نحوه اجراي اين ماده توسط حسابرسان نظارت كند.
                </p>
                <p class="mx-auto">
                    تبصره ۴ – دستگاه ‌هاي اجرايي و مؤسسات مالي و اعتباري مكلفند اطلاعات مورد نياز حسابرسان را براي اجراي تكاليف اين ماده در اختيار آنان قرار دهند. مصاديق اين اطلاعات در اين تبصره توسط مركز با همكاري سازمان حسابرسي و جامعه حسابداران رسمي ايران تعيين خواهد شد.
                </p>
                <p class="mx-auto">
                    تبصره ۵ - سازمان حسابرسي و جامعه حسابداران رسمي ايران مكلفند زيرساخت‌ هاي مورد نياز را جهت اجراي مفاد اين ماده فراهم كنند.
                </p>
                <p class="mx-auto">
                    ماده 47 - صدور و تمديد هرگونه مجوز صنفي براي مشاغل ‌غيرمالي منوط به اخذ تعهد مبني بر رعايت قوانين مبارزه با پولشويي و تأمين مالي تروريسم است و نحوه احراز صلاحيت متقاضيان و صاحبان مشاغل غيرمالي در اين زمينه بنا بر ساز و كار اعلامي توسط مركز با همكاري وزارت صنعت‌، معدن و تجارت مشخص خواهد شد.
                </p>
                <p class="mx-auto">
                    ماده 48 - دستگاه‌ هاي متولي نظارت مكلفند در چهارچوب اولويت ‌هاي تعيين ‌شده توسط مركز، برنامه سالانه مبارزه با پولشويي و تأمين مالي تروريسم دستگاه متبوع خود را جهت بررسي و تأييد به مركز ارايه كنند.
                </p>
                <p class="mx-auto">
                    تبصره - در پايان هر سال، دستگاه‌ هاي متولي نظارت مكلفند گزارشي از اقدامات انجام ‌شده و ميزان پيشرفت برنامه‌ هاي ارايه ‌شده را جهت بررسي به مركز ارايه كنند.
                </p>
                <p class="mx-auto">
                    اده 49 - اشخاص مشمول مكلفند سامانه (سيستم) جامعي از مديريت اطلاعات را ايجاد كنند كه امكان انجام تكاليف محول شده اعم از واپايش (كنترل)، پايش و كشف تقلب را براي واحدهاي مبارزه با ‌پولشويي فراهم آورد.
                </p>
                <p class="mx-auto">
                    فصل پنجم - شناسايي ارباب‌ رجوع
                </p>
                <p class="mx-auto">
                    ماده 50 - اشخاص مشمول مكلفند پيش از برقراري هرگونه تعامل كاري، رويه‌ هاي شناسايي مقتضي را انجام دهند و در مواردي كه انجام اين رويه‌ ها امكان‌ پذير نيست، برقراري هرگونه تعامل كاري ممنوع است و تخلف محسوب مي‌ شود.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - ارايه خدمات به ارباب‌ رجوع به‌ منزله تضمين انجام رويه‌ هاي شناسايي مقتضي ارباب‌ رجوع توسط كاركنان اشخاص مشمول است و مسئوليت وجود هرگونه تخطي در اين زمينه بر عهده اشخاص مشمول و كاركنان ذي ‌ربط است.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - اشخاص مشمول مكلفند نگهداري و استمرار در ارايه خدمات پايه را به ‌صورت بي‌ نام و يا با هويت مجهول يا جعلي و همچنين انجام هرگونه تراكنش و معاملات مالي الكترونيكي بي‌ نام يا غيرقابل رديابي را متوقف كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۳ - اشخاص مشمول مكلفند از ارايه خدمت به اشخاص فاقد هويت قانوني يا اشخاصي كه شناسه يكتاي هويتي آنها به ‌دلايلي اعم از فوت و غيره توسط مراجع ذي‌ ربط ابطال شده است، خودداري كنند.
                </p>
                <p class="mx-auto">
                    ماده 51 - اشخاص مشمول مكلفند جهت اجراي فرايندهاي شناسايي، خطر (ريسك) تعاملات كاري ارباب‌ رجوع را بر‌ اساس مقررات مبارزه با پولشويي و تأمين مالي تروريسم طبقه‌ بندي كنند و رويه ‌هاي شناسايي را متناسب با اين خطر (ريسك) در سه سطح ساده،‌ معمول و مضاعف به اجرا گذارند. همچنين، سياست‌ ها،‌ خط‌ مشي‌ ها و رويه‌ هاي شناسايي ارباب ‌رجوع بايد مبتني بر رويكرد خطر (ريسك) ‌محور باشد تا نظارت مستمر و هدفمند بر تعامل كاري ارباب ‌رجوع پديد آيد و سطح اطلاعات اخذ شده از ارباب‌ رجوع (‌ازجمله اطلاعات مالك واقعي اشخاص حقوقي) متناسب با خطر (ريسك) وي تعيين، نگهداري و به‌ روزرساني شود.
                </p>
                <p class="mx-auto">
                    ماده 52 - مؤسسات مالي و اعتباري و صرافي‌ ها مكلفند در اجراي شناسايي ارباب ‌رجوع، فرايندها و رويه‌ هاي شناسايي مقتضي (شامل شناسايي ساده، معمول و مضاعف) را به‌ گونه ‌اي ساماندهي كنند كه حين اخذ اطلاعات كافي در هنگام آغاز تعامل كاري (‌همچنين در طول تعامل كاري)، امكان ارزيابي خطر (ريسك) برقراري تعامل كاري با ارباب ‌رجوع و اتخاذ رويه‌ هاي شناسايي متناسب فراهم شود.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - اطلاعات مورد نياز بايد حداقل شامل مواردي نظير نوع شخص (حقيقي يا حقوقي)، تابعيت ارباب‌ رجوع، منطقه جغرافيايي محل سكونت و محل اقامت قانوني، مبدأ و مقصد مورد انتظار تراكنش ‌ها و عمليات ارباب‌ رجوع، شغل و نوع فعاليت، ميزان درآمد و منابع اموال، خدمات مورد تقاضا يا ارايه ‌شده از سوي ارباب‌ رجوع، هدف از افتتاح حساب يا ايجاد تعامل كاري، استفاده مستقيم يا با واسطه از خدمات دريافتي و استفاده يا دريافت خدمات به ‌صورت غيرحضوري باشد كه ماهيت، هدف و خطر (ريسك) تعامل كاري با ارباب‌ رجوع را مشخص سازد.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - ارايه خدمات ‌غيرپايه كمتر از سقف مقرر به ارزيابي خطر (ريسك) برقراري تعامل كاري با ارباب ‌رجوع نياز ندارد.
                </p>
                <p class="mx-auto">
                    ماده 53 - اشخاص مشمول مي‌ توانند پس از انجام يك ارزيابي جامع خطر (ريسك)، تنها در مواردي كه خطر (ريسك) تعاملات كاري پايين‌ ارزيابي مي ‌شود به انجام رويه‌ هاي شناسايي ساده اكتفا كنند. در ساير موارد، انجام رويه‌ هاي شناسايي معمول و يا شناسايي مضاعف مطابق با مقررات آيين ‌نامه الزامي است.
                </p>
                <p class="mx-auto">
                    تبصره - اشخاص مشمول مكلفند مصاديق آن دسته از تعاملات كاري كه در خصوص آنها رويه ‌هاي شناسايي ساده به اجرا گذاشته مي ‌شود، به تأييد دستگاه ‌هاي متولي نظارت برسانند.
                </p>
                <p class="mx-auto">
                    ماده 54 - به ‌منظور انجام رويه‌ هاي شناسايي ساده، اشخاص مشمول مكلفند تدابير سهل ‌گيرانه شناسايي ارباب‌ رجوع را متناسب با سطح خطر (ريسك) ارباب‌ رجوع اتخاذ كنند. اين تدابير نبايد ناقض ساير مقررات مبارزه با پولشويي و تأمين مالي تروريسم باشد. برخي از اين تدابير عبارتند از:
                </p>
                <p class="mx-auto">
                    ۱ - كاهش تعداد دفعات به ‌روزرساني اطلاعات شناسايي ارباب‌ رجوع.
                </p>
                <p class="mx-auto">
                    ۲ - كاهش ميزان پايش مستمر و بررسي موشكافانهٴ تراكنش‌ ها بر اساس سطح فعاليت مورد انتظار ارباب‌ رجوع؛
                </p>
                <p class="mx-auto">
                    3 - عدم جمع ‌آوري اطلاعات دقيق و با جزئيات، يا انجام اقدامات خاص براي شناخت هدف و ماهيت مورد نظر روابط تجاري، در مواردي كه هدف و ماهيت تراكنش‌ ها از طريق نوع تراكنش ‌ها يا روابط كاري ايجاد شده قابل استنباط باشد.
                </p>
                <p class="mx-auto">
                    ماده 55 - اشخاص مشمول مكلفند هنگام ارائه همهٴ تعاملات كاري حتي كمتر از سقف مقرر از جمله انجام هرگونه دريافت و پرداخت، حواله وجه، صدور و پرداخت چك، ارائهٴ تسهيلات، صدور انواع كارت دريافت و پرداخت، صدور انواع ضمانت‌ نامه، خريد و فروش ارز و اوراق گواهي سپرده و اوراق مشاركت، قبول ضمانت و تعهد ضامنين به هر شكل از قبيل امضاي سفته، بروات و اعتبارات اسنادي، خريد و فروش سهام، ارائه انواع خدمات بيمه‌ اي، خدمات واسپاري، خريد اوراق سرمايه‌ گذاري، ارائه انواع خدمات مالياتي از جمله تهيه و تنظيم اظهارنامهٴ مالياتي و تحرير دفاتر قانوني و ارائهٴ انواع خدمات از سوي نهادهاي عمومي غيردولتي، به اجراي فرايند احراز هويت ارباب‌ رجوع (اعم از دائم/ گذري، حقيقي/ حقوقي) اقدام و اطلاعات آن را در دفاتر و يا سامانه (سيستم)‌ هاي اطلاعاتي خود ثبت كنند. اين اقدام بايد پيش از ايجاد تعامل كاري و ارائهٴ هر محصول يا خدمت يا انجام هرگونه تراكنش انجام شود.
                </p>
                <p class="mx-auto">
                    تبصره - پرداخت قبوض دولتي و خدمات شهري كمتر از سقف مقرر و ساير موارد اعلامي شورا به احراز هويت نيازي ندارد.
                </p>
                <p class="mx-auto">
                    اده 56 - اشخاص مشمول مكلفند به ‌منظور اجراي فرايند احراز هويت ارباب‌ رجوع، به اخذ اطلاعات هويتي وي و بررسي تطابق اين اطلاعات با مدارك شناسايي معتبر اقدام كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۱ – به ‌منظور احراز هويت اشخاصي كه به نمايندگي از شخص اصيل تحت هر عنوان (از جمله وليّ، وصي، قيم، وكيل و نمايندهٴ شخص حقوقي) به اشخاص مشمول مراجعه مي ‌كنند، اشخاص مشمول مكلفند علاوه بر اجراي فرايند احراز هويت در خصوص نماينده، اسناد معتبر و رسمي مبين نمايندگي مانند وكالت ‌نامه را اخذ و تطابق آن را بررسي كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - در خصوص خدمات الكترونيكي و غيرحضوري (مانند خدمات اينترنتي)‌، پذيرش ابزار پرداخت‌، دريافت اطلاعات مرتبط (مانند دريافت رمز، تاريخ انقضاي كارت) و انطباق آ‌ن‌ ها به ‌منزلهٴ اجراي فرايند احراز هويت است.
                </p>
                <p class="mx-auto">
                    تبصره ۳ - مدارك شناسايي معتبر در خصوص اشخاص حقيقي و حقوقي ايراني توسط مركز تعيين و اعلام مي ‌شود.
                </p>
                <p class="mx-auto">ماده 57 - در خصوص اشخاص حقوقي در شرف تأسيس، مؤسسات مالي و اعتباري مي ‌توانند بدون دريافت شناسهٴ ملي به افتتاح حساب اقدام كنند، منوط به اين امر كه معرفي ‌نامهٴ رسمي سازمان ثبت اسناد و املاك كشور را اخذ و حساب مذكور را تا زمان دريافت شناسهٴ ملي جهت برداشت مسدود نمايند.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - در صورت انصراف اشخاص حقوقي در شرف تأسيس از ثبت رسمي، مؤسسات مالي و اعتباري صرفاً با اعلام سازمان ثبت اسناد و املاك كشور مي ‌توانند به بستن حساب و استرداد وديعهٴ سپرده ‌گذاري ‌شده اقدام كنند. وديعهٴ سپرده ‌گذاري‌ شده صرفاً به شخص حقيقي افتتاح ‌كنندهٴ حساب يا نمايندهٴ مجاز اعلامي توسط سازمان ثبت اسناد و املاك كشور بازپرداخت مي ‌شود.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - بانك مركزي جمهوري اسلامي ايران مكلف است با همكاري سازمان ثبت اسناد و املاك كشور ظرف سه ماه پس از تصويب آيين ‌نامه، زيرساخت سامانه (سيستمي) لازم را جهت اجراي اين ماده و تبادل اطلاعات اشخاص حقوقي در شرف تأسيس فراهم آورد.
                </p>
                <p class="mx-auto">
                    ماده 58 - مؤسسات مالي و اعتباري و صرافي‌ ها مكلفند در موارد زير نسبت به انجام يا به ‌روزرساني رويه ‌هاي شناسايي معمول اقدام كنند:
                </p>
                <p class="mx-auto">
                    ۱ - پيش از ارائهٴ خدمات پايه (اعم از افتتاح حساب و ...)؛
                </p>
                <p class="mx-auto">
                    ۲ ‌- پيش از ارائهٴ خدمات ‌غيرپايه و برقراري تعامل كاري با ارباب‌ رجوع گذري براي تراكنش‌ هاي بالاتر از سقف مقرر، بدون توجه به اينكه تراكنش ‌به‌ عنوان يك تراكنش واحد و يا چند تراكنش مرتبط به هم انجام مي ‌شود؛
                </p>
                <p class="mx-auto">
                    ۳ - پيش از انجام نقل ‌و انتقالات الكترونيكي بين ‌المللي؛
                </p>
                <p class="mx-auto">
                    ۴ - وجود سوء ‌ظن در مورد پول‌ شويي و يا تأمين مالي تروريسم؛
                </p>
                <p class="mx-auto">
                    ۵ - وجود شك و ترديد در مورد صحت يا كفايت اطلاعاتِ از قبل دريافت ‌شده از ارباب ‌رجوع؛
                </p>
                <p class="mx-auto">
                    ۶ - وجود دلايلي مبني بر تغيير در وضعيت و اطلاعات ارباب‌ رجوع حين انجام رويه‌ هاي نظارت و پايش.
                </p>
                <p class="mx-auto">
                    تبصره - به‌ منظور اجراي رويه‌ هاي شناسايي معمول، فواصل زماني به ‌روزرساني اطلاعات براي ارباب ‌رجوع نبايد بيش از يك سال به طول انجامد؛ در خصوص اشخاص پرخطر (پرريسك) بايد اين به ‌روزرساني در فواصل زماني كوتاه‌ تري انجام پذيرد.
                </p>
                <p class="mx-auto">
                    ماده 59 - اشخاص مشمول مكلفند به ‌منظور انجام رويه ‌هاي شناسايي معمول، تدابير مناسبي اتخاذ كنند تا از انجام اقدامات زير در خصوص ارباب ‌رجوع اطمينان حاصل شود:
                </p>
                <p class="mx-auto">
                    1 - احراز هويت ارباب ‌رجوع تنها با استفاده از منابع قابل اطمينان و مستقل و اخذ مدارك معتبر؛
                </p>
                <p class="mx-auto">
                    ۲ - اطمينان از شناسايي مالك واقعي مبتني بر اطلاعات و اسناد قابل اتكا؛
                </p>
                <p class="mx-auto">
                    ۳ - شناخت ماهيت، هدف و ميزان فعاليت ارباب ‌رجوع طي برقراري تعامل كاري؛
                </p>
                <p class="mx-auto">
                    ۴ - بررسي اسناد و مدارك مثبتهٴ اشخاصي كه تحت هر عنوان (ازجمله وليّ، وصي، قيم، وكيل و نمايندهٴ شخص حقوقي) به نمايندگي از اشخاص ديگر به مؤسسات مالي و اعتباري مراجعه كرده ‌اند، علاوه بر انجام رويه‌ هاي احراز هويت در خصوص اين اشخاص؛
                </p>
                <p class="mx-auto">
                    ۵ - اجراي فرايند شناسايي معمول به ‌صورت مستمر با انجام اقدامات مقتضي نظير اطمينان از بهنگام ‌بودن اطلاعات اخذ شده از ارباب ‌رجوع، پايش مستمر ارباب ‌رجوع بر اساس سطح فعاليت مورد انتظار ارباب ‌رجوع و تطابق تراكنش‌ هاي ارباب ‌رجوع با اطلاعات اخذ شده از آن ‌ها؛
                </p>
                <p class="mx-auto">
                    ۶ – جمع ‌آوري و نگهداري اطلاعات مربوط به ارزيابي خطر (ريسك) تعاملات كاري.
                </p>
                <p class="mx-auto">
                    ماده 60 - مؤسسات مالي و اعتباري مكلفند به‌ منظور اجراي رويه ‌هاي شناسايي معمول در خصوص اشخاص حقيقي، تدابير حداقلي زير را رعايت كنند و سه دستهٴ كلي اطلاعات هويتي، اقتصادي و مالك واقعي موضوع اين ماده را همراه با اسناد و مدارك معتبر، حسب مورد از ارباب ‌رجوع يا دستگاه‌ هاي ذي ‌ربط اخذ و پس از اطمينان از صحت و اصالت اطلاعات، آن ‌ها را به ‌صورت سامانه اي (سيستمي) در رخ نماي (پروفايل) مشتريان خود نگهداري كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - حداقل اطلاعات هويتي، اقتصادي و مالك واقعي در خصوص اشخاص حقيقي ايراني به شرح زير است:
                </p>
                <p class="mx-auto">
                    ۱ - احراز هويت ارباب ‌رجوع و اخذ اطلاعات هويتي اعم از نام و نام خانوادگي، سال تولد، شمارهٴ ملي، نام پدر، وضعيت حيات و در صورت فوت تاريخ آن، شناسه (كد) پستي محل اقامت، با دريافت اسناد و مدارك شناسايي معتبر؛
                </p>
                <p class="mx-auto">
                    ۲ - انجام اقدامات مقتضي (نظير دريافت اظهار كتبي و اخذ تعهد مبني بر صحت اظهارات) جهت تعيين اينكه ارباب ‌رجوع از طرف خود يا از طرف مالكان واقعي ديگري فعاليت مي ‌كند و در صورت وجود مالك واقعي، اخذ اطلاعات هويتي مالك واقعي طبق مقررات اين ماده
                </p>
                <p class="mx-auto">
                    ۳ - اطلاعات اقتصادي شامل اطلاعات شغلي (مانند مشخصات كارفرما/ سازمان/ مؤسسهٴ محل اشتغال در خصوص كارمندان و كارگران و عنوان فعاليت شغلي و شمارهٴ مجوز فعاليت در خصوص مشاغل آزاد)، پيش ‌بيني از ميزان درآمد سالانه، پيش ‌بيني از مجموع مبالغ سالانهٴ واريزي و برداشتي از حساب، پيش‌ بيني از حداكثر مبلغ هر تراكنش، اطلاعات ساير منابع درآمدي (مانند شناسه (كد) معاملاتي بورس اوراق بهادار، شناسه (كد) رهگيري اجاره‌ نامهٴ املاك و مستغلات)، گزارش بازرس و حسابرس قانوني (در صورت اجباري ‌بودن داشتن بازرس يا حسابرس قانوني براي ارباب ‌رجوع)، اظهارنامهٴ مالياتي و شناسه (كد) اقتصادي (در صورت وجود).
                </p>
                <p class="mx-auto">
                    تبصره ۲ - اطلاعات هويتي، اقتصادي و مالك واقعي در خصوص اشخاص حقيقي خارجي به شرح زير است:
                </p>
                <p class="mx-auto">
                    ۱ - احراز هويت ارباب ‌رجوع و اخذ اطلاعات هويتي نام و نام خانوادگي، نام پدر، نام جد، شمارهٴ اختصاصي، تاريخ و محل تولد، وضعيت حيات و تاريخ فوت، وضعيت اقامت شخص و تاريخ ورود وي به كشور، شناسه (كد) پستي محل اقامت، نوع مدرك هويتي، مدت اعتبار مدارك هويتي، مليت، با دريافت اسناد و مدارك شناسايي معتبر؛
                </p>
                <p class="mx-auto">
                    ۲ - انجام اقدامات مقتضي (نظير دريافت اظهار كتبي و اخذ تعهد مبني بر صحت اظهارات) جهت تعيين اينكه ارباب‌ رجوع از طرف خود يا از طرف مالكان واقعي ديگري فعاليت مي‌ كند و در صورت وجود مالك واقعي، اخذ اطلاعات هويتي مالك واقعي طبق مقررات اين ماده.
                </p>
                <p class="mx-auto">
                    3 - اطلاعات اقتصادي شامل اطلاعات شغلي و مجوز فعاليت اخذ شده از وزارت تعاون، كار و رفاه اجتماعي (مانند مشخصات كارفرما/ سازمان/ مؤسسهٴ محل اشتغال در خصوص كارمندان و كارگران و عنوان فعاليت شغلي و شمارهٴ مجوز فعاليت در خصوص مشاغل آزاد)، پيش‌ بيني از ميزان درآمد سالانه، پيش‌ بيني از مجموع مبالغ سالانهٴ واريزي و برداشتي از حساب، پيش ‌بيني از حداكثر مبلغ هر تراكنش، اطلاعات ساير منابع درآمدي (مانند شناسه (كد) معاملاتي بورس اوراق بهادار، شناسه (كد) رهگيري اجاره‌ نامهٴ املاك و مستغلات)، گزارش بازرس و حسابرس قانوني (در صورت اجباري بودن داشتن بازرس يا حسابرس قانوني براي ارباب رجوع)، اظهارنامهٴ مالياتي و شناسه (كد) اقتصادي (در صورت وجود).
                </p>
                <p class="mx-auto">
                    ماده 61 - مؤسسات مالي و اعتباري مكلفند به ‌منظور اجراي رويه ‌هاي شناسايي معمول در خصوص اشخاص حقوقي، تدابير حداقلي زير را رعايت كنند و سه دستهٴ كلي اطلاعات هويتي، اقتصادي و مالك واقعي را به شرح اين ماده همراه با اسناد و مدارك معتبر، حسب مورد از ارباب ‌رجوع يا دستگاه ‌هاي ذي ‌ربط اخذ و پس از اطمينان از صحت و اصالت اطلاعات، آن ‌ها را به ‌همراه اسناد مربوط به‌ صورت سامانه اي (سيستمي) در رخ نماي (پروفايل) مشتريان خود نگهداري كنند.
                </p>
                <p class="mx-auto">
                    احراز هويت ارباب ‌رجوع و اخذ اطلاعات هويتي زير با دريافت اسناد و مدارك:
                </p>
                <p class="mx-auto">
                    الف - شناسهٴ ملي، نام شخص حقوقي، نوع شخص حقوقي (تجارتي يا غيرتجارتي/ انتفاعي يا ‌غيرانتفاعي/ دولتي يا غيردولتي/ در خصوص شركت‌ هاي تجارتي نوع آن اعم از مسئوليت محدود، سهامي عام، سهامي خاص و غيره)، نهاد ثبت ‌كننده (اعم از ثبت شركت‌ ها و مؤسسات غيرتجارتي، وزارت كشور، و غيره)، تاريخ تأسيس، وضعيت (اعم از فعال، منحل‌ شده و ختم ‌تصفيه ‌شده) و تاريخ انحلال/ ختم ‎تصفيه با تكيه بر اسناد و مدارك معتبر دال بر موجوديت شخص حقوقي نظير اساسنامه و شركت ‌نامه؛
                </p>
                <p class="mx-auto">
                    ب - ساختار مالكيت و ساختار واپايشي (كنترلي) ارباب ‌رجوع شامل اطلاعات سهامداران عمده، اعضاي هيئت‌ مديره، مديران ارشد اجرايي و حسب مورد بازرسان يا حسابرسان با تكيه بر مقررات حاكم بر شخص حقوقي كه به آن ‌ها انتظام مي ‌دهد و بر اساس آن نظارت مي ‌شوند (نظير اساسنامه)؛
                </p>
                <p class="mx-auto">
                    پ - نشاني و شناسه (كد) پستي احراز شدهٴ محل اقامت قانوني دفتر مركزي و محل اصلي فعاليت با تكيه بر اسناد و مدارك معتبر دال بر موجوديت شخص حقوقي.
                </p>
                <p class="mx-auto">
                    ۲ - شناخت مالك واقعي اشخاص حقوقي بر اساس اخذ اطلاعات هويتي اشخاص زير:
                </p>
                <p class="mx-auto">
                    الف - اشخاص حقيقي يا آن گروه از اشخاص حقوقي كه ‌مستقيم يا غيرمستقيم حداقل داراي منفعت (۲۵) درصد از مالكيت سهام يا يك كرسي مديريتي در هيئت ‌مديرهٴ شخص حقوقي باشند، مالك واقعي شناخته مي ‌شوند.
                </p>
                <p class="mx-auto">
                    ب - اشخاصي كه با استفاده از ابزارهاي ديگر نظير وكالت‌ نامه به واپايش (كنترل) اشخاص حقوقي مي‌ پردازند، در صورت ترديد در مورد بند (الف) يا نبودِ شخص حقيقي كه از طريق مالكيت امكان كنترل اشخاص حقوقي را داشته باشد.
                </p>
                <p class="mx-auto">
                    پ - مديران ارشد اشخاص حقوقي در صورت تحقق ‌نيافتن بندهاي (الف) و (ب).
                </p>
                <p class="mx-auto">
                    ۳ - شناخت موضوع، ماهيت و ميزان فعاليت ارباب ‌رجوع با اخذ اطلاعاتي نظير زمينهٴ فعاليت (مانند توليدي و بازرگاني)، پيش‌ بيني از منابع درآمد مانند درآمد حاصل از سرمايه ‌گذاري و فروش محصولات، پيش ‌بيني از ميزان فروش، هزينه و درآمد، پيش ‌بيني از ميزان صادرات و واردات، پيش ‌بيني از ميزان گردش حساب مورد انتظار سالانه و پيش‌ بيني از تعداد تراكنش‌ هاي مورد انتظار سالانه با تكيه بر اسناد و مدارك معتبر اعم از مجوز فعاليت شخص حقوقي (جواز تأسيس، جواز كسب، پروانهٴ بهره‌ برداري يا كارت بازرگاني و مجوز نماد اعتماد الكترونيكي)، آخرين اظهارنامهٴ مالياتي و يا يكي از گزارش‌ هاي منضم به صورت‌ هاي مالي كه قابل‌ِ استعلام از جامعهٴ حسابداران رسمي ايران باشد.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - براي اشخاص حقوقي كه سهام آن ‌ها در بورس عرضه مي‌ شود و مالكيت آن ‌ها شفاف است، نيازي به شناسايي و تأييد هويت سهامداران و مالك واقعي نيست.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - بانك مركزي جمهوري اسلامي ايران مكلف است اطلاعات لازم را در خصوص اجراي اين ماده از طريق سامانهٴ موضوع مادهٴ (۲۹) اين آيين ‌نامه در اختيار اشخاص مشمول قرار دهد.
                </p>
                <p class="mx-auto">
                    تبصره ۳ - مؤسسات ارائه ‌دهندهٴ خدمات بيمه مكلف ‌اند علاوه بر انجام رويه‌ هاي شناسايي (ساده ‌شده، معمول، يا مضاعف) مانند شناسايي مالك واقعي، تدابير مربوط به شناسايي ذي‌ نفعان بيمهٴ عمر و يا ساير موارد، سرمايه‌ گذاري را به شرح زير انجام دهند:
                </p>
                <p class="mx-auto">
                    ۱ - شناسايي ذي ‌نفعاني كه نام آن ‌ها در بيمه ‌نامه ‌به ‌طور صريح تعيين شده است (اعم از اشخاص حقيقي و يا حقوقي)؛
                </p>
                <p class="mx-auto">
                    ۲ - شناسايي ذي ‌نفعاني كه نام آن ‌ها با طبقه يا ويژگي خاصي ذكر شده است (مانند همسر يا فرزند در هنگام بيمه ‌گذاري) و يا ساير ابزارهاي حقوقي (مانند وصيت‌ نامه)،‌ لازم است اطلاعات كافي در مورد ذي‌ نفع اخذ شود تا مؤسسهٴ بيمه ‌گذار متقاعد شود در هنگام پرداخت قادر به شناسايي ذي ‌نفع است.
                </p>
                <p class="mx-auto">
                    تبصره ۴ - در مورد مؤسسات و تشكيلات دولتي و بلدي (موضوع مادهٴ (۵۸۷) قانون تجارت) كه به‌ محض ايجاد و بدون احتياج به ثبت داراي شخصيت حقوقي مي ‌شوند، رويهٴ شناسايي مقتضي طبق ساز و كار اعلامي مركز خواهد بود.
                </p>
                <p class="mx-auto">
                    ماده 62 - اشخاص مشمول مكلف ‌اند در مواردي كه ظن به پول ‌شويي و يا تأمين مالي تروريسم وجود داشته باشد، چنانچه انجام فرايندهاي شناسايي معمول باعث شك ارباب ‌رجوع مي‌ شود، ضمن مديريت فرايند شناسايي و انجام اقدامات مقتضي جهت جلب اطمينان وي، مراتب را فوراً به مركز گزارش و مطابق دستورات مركز عمل كنند.
                </p>
                <p class="mx-auto">
                    ماده 63 - دستگاه‌ هاي متولي نظارت مكلف‌ اند با همكاري مركز ظرف سه ماه پس از تصويب اين آيين ‌نامه، ضوابط استاندارد مربوط به اخذ اطلاعات هويتي، اقتصادي و مالك واقعي در ‌هر يك از حوزه‌ هاي اصلي را مطابق با مقررات پول ‌شويي و تأمين مالي تروريسم تدوين و ابلاغ كنند. اشخاص مشمول مكلف ‌اند پس از ابلاغ اين ضوابط، آن‌ ها را در طراحي‌ برگه (فرم‌) هاي اخذ اطلاعات هويتي، اقتصادي و مالك واقعي از ارباب‌ رجوع لحاظ كنند.
                </p>
                <p class="mx-auto">
                    ماده 64 - اشخاص مشمول مكلف ‌اند به‌ منظور مستند‌سازي و راستي ‌آزمايي اطلاعات ارباب ‌رجوع، صحت و اصالت اطلاعات و اسناد اخذ ‌شده از وي را از مراجع ذي‌ ربط استعلام و ثبت كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - تا زمان راه ‌اندازي سامانه‌ ها و پايگاه ‌هاي اطلاعاتي مربوط و در مواردي كه زيرساخت‌ هاي لازم وجود ندارد، تصوير اسناد و مدارك مرتبط اخذ ‌شده از ارباب ‌رجوع توسط صاحب امضاي مجاز مستقر در اشخاص مشمول برابر اصل مي ‌گردد.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - منظور از مراجع ذي ‌ربط، سامانه ‌ها و پايگاه ‌هاي اطلاعاتي مندرج در فصل (۳) اين آيين ‌نامه است. هرگونه تغيير در فهرست اين پايگاه ‌ها و همچنين نحوه و اقلام اطلاعاتي مورد استعلام توسط مركز تعيين و اعلام خواهد شد.
                </p>
                <p class="mx-auto">
                    تبصره ۳ - اشخاص مشمول مكلف‌ اند در صورت عدم انطباق اطلاعات هويتي (اعم از نام‌، نام خانوادگي‌، شناسه (كد) پستي و...) ارائه ‌شده توسط ارباب ‌رجوع با استعلام ‌هاي صورت‌ پذيرفته، از ارائهٴ هرگونه خدمت تا اصلاح مغايرت خودداري كنند.
                </p>
                <p class="mx-auto">
                    تبصره 4 - همهٴ اشخاص مشمول مكلف‌ اند حداكثر يك سال پس از تصويب اين آيين ‌نامه، به ‌منظور ارسال هرگونه پيام يا برقراري ارتباط غيرحضوري با ارباب ‌رجوع، فقط از زيرساخت مبتني بر تلفن همراه موضوع سامانهٴ مادهٴ (۲۲) اين آيين نامه استفاده كنند.
                </p>
                <p class="mx-auto">
                    ماده 65 - اشخاص مشمول مكلفند ارائهٴ هرگونه خدمت به اشخاصي كه به نمايندگي از شخص اصيل تحت هر عنوان (ازجمله وليّ، وصي، قيم و وكيل) به آن ‌ها مراجعه مي‌ كنند، به اجراي فرايند احراز هويت نماينده و اخذ اسناد رسمي معتبر مبين نمايندگي (مانند وكالت‌ نامهٴ تنظيم ‌شده در دفاتر اسناد رسمي و احكام مراجع قضايي مبني بر وصايت و قيموميت) منوط نمايند.
                </p>
                <p class="mx-auto">
                    تبصره - اشخاص مشمول مكلف‌ اند به‌ منظور اجراي فرايند شناسايي معمول، پيش از ارائهٴ هرگونه خدمت به نمايندهٴ شخص اصيل، نسبت به استعلام صحت و اصالت مدارك شناسايي معتبر نماينده و اسناد رسمي مبين نمايندگي وي از سامانه ‌هاي مربوط اقدام و اطلاعات احراز شدهٴ نماينده را نيز در رخ نماي (پروفايل) ارباب‌ رجوع ثبت كنند.
                </p>
                <p class="mx-auto">
                    ماده 66 - ارائه‌ دهندگان خدمات پايه مكلف‌ اند رويه ‌هايي را اتخاذ كنند كه در كمتر از مدت يك ماه تغييراتي كه منجر به ابطال يا تعليق شناسهٴ هويتي در شخص حقيقي يا حقوقي مي ‌شود (نظير فوت، حجر، انحلال و يا ممنوع‌ المعامله ‌شدن) را احراز و ادامهٴ ارائهٴ خدمت را به ‌صورت نظام ‌مند متوقف كنند. در صورتي ‌كه پس از ابطال يا تعليق شناسهٴ هويتي، تراكنش يا معاملهٴ بالاتر از سقف مقرر توسط ارباب ‌رجوع انجام شده باشد، اشخاص مشمول مكلف‌ اند مراتب را به مركز گزارش دهند.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - سازمان ثبت احوال كشور، سازمان ثبت اسناد و املاك كشور و وزارت اطلاعات مكلف‌ اند اطلاعات لازم را در خصوص اجراي اين ماده در اختيار اشخاص مشمول قرار دهند.
                </p>
                <p class="mx-auto">
                    تبصره ۲ – ارائه ‌دهندگان خدمات پايه مكلف‌ اند به ‌محض برطرف ‌شدن عامل انسداد حساب با قيد فوريت، نسبت به رفع محدوديت از ارائهٴ خدمات اقدام كنند.
                </p>
                <p class="mx-auto">
                    ماده 67 - اشخاص مشمول مكلف ‌اند به ‌منظور پايش مستمر ارباب‌ رجوع در فرايند شناسايي معمول و نيز ارزيابي خطرپذيري (ريسك) برقراري تعاملات كاري با آن ‌ها، نسبت به تعيين سطح فعاليت ارباب ‌رجوع مطابق اين آيين ‌نامه و ساير الزامات تعيين ‌شده توسط مركز با همكاري دستگاه‌ هاي متوليِ نظارت‌ اقدام كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۱ – دستگاه ‌هاي متولي نظارت مكلف‌ اند از طريق سامانهٴ موضوع مادهٴ (۲۶) اين آيين نامه نسبت به صحت‌ سنجي اطلاعات و ارزيابي تناسب سطح فعاليت اقدام كنند. در صورت عدم تأييد اين موارد، اشخاص مشمول مكلف ‌اند فوراً نسبت به تعيين سطح مجدد ارباب ‌رجوع و ارسال اسناد مثبتهٴ مربوط به دستگاه متوليِ نظارت اقدام كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - در صورت مغايرت رفتار مالي ارباب ‌رجوع با سطح فعاليت مورد انتظار تعيين ‌شده، اشخاص مشمول مكلف‌ اند نسبت به دعوت از ارباب ‌رجوع و ارائهٴ برگه (فرم) مغايرت با سطح فعاليت به ايشان اقدام و برگه (فرم) مربوط را پس از تكميل، بررسي كنند. اشخاص مشمول مكلف‌ اند در صورت تشخيص صحت ادعاي ارباب رجوع، نسبت به به‌ روزرساني سطح فعاليت مورد انتظار وي اقدام كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۳ - در صورت مغايرت رفتار مالي ارباب‌ رجوع، مؤسسات مالي و اعتباري تا زمان مراجعهٴ ارباب ‌رجوع مكلف به اعمال محدوديت‌ هاي زير هستند:
                </p>
                <p class="mx-auto">
                    الف - اعمال محدوديت در خصوص ارائهٴ خدمت به همهٴ ابزارهاي پرداخت ارباب ‌رجوع؛
                </p>
                <p class="mx-auto">
                    ب – منوط ‌شدن انجام همهٴ معاملات و عمليات بانكي به مراجعهٴ حضوري ارباب‌ رجوع نزد مؤسسهٴ مالي و اعتباري و درج بابت در برگه (فرم‌) هاي مربوط و ارائهٴ اسناد مثبته.
                </p>
                <p class="mx-auto">
                    تبصره ۴ - دستگاه متولي نظارت مكلف است با هماهنگي و تأييد مركز‌، برگه (فرم) موضوع تبصره (۳) اين ماده را ظرف سه ماه پس از تصويب اين آيين ‌نامه تدوين كند و در اختيار اشخاص مشمول قرار دهد.
                </p>
                <p class="mx-auto">
                    تبصره 5 - در خصوص اشخاصي كه از ارائهٴ اطلاعات اقتصادي مطابق با الزامات آيين ‌نامه استنكاف مي‌ كنند، سطح فعاليت مورد انتظار به ‌صورت حداقلي مطابق با ضوابط اعلامي مركز تعيين خواهد شد.
                </p>
                <p class="mx-auto">
                    ماده 68 - اشخاص مشمول مكلف ‌اند نسبت به اتخاذ تدابير زير در خصوص ارائهٴ خدمت صندوق امانات و صندوق پستي اقدام كنند:
                </p>
                <p class="mx-auto">
                    ۱ - انجام رويه ‌هاي شناسايي معمول پيش از ارائهٴ صندوق امانات و صندوق پستي.
                </p>
                <p class="mx-auto">
                    ۲ - استقرار سامانه (سيستم) يكپارچه به‌ منظور تجميع اطلاعات مشتريان صندوق امانات (اعم از اطلاعات شناسايي اجاره ‌كننده (هاي) صندوق و اطلاعات مربوط به وكالت و نمايندگي اشخاص)، به‌ گونه ‌اي كه سامانه (سيستم) مذكور بتواند اطلاعات مورد نظر مراجع ذي ‌ربط را فوراً و در چهارچوب قوانين و مقررات مربوط در اختيار آن ‌ها قرار دهد؛
                </p>
                <p class="mx-auto">
                    3 - ثبت زمان تمام مراجعات ارباب‌ رجوع جهت استفاده از صندوق امانت، به ‌گونه ‌اي كه مراجعات مربوط به هر ارباب ‌رجوع به‌ صورت سامانه اي (سيستمي) قابل مشاهده و گزارش ‌گيري باشد؛
                </p>
                <p class="mx-auto">
                    4 - چنانچه ظن قوي به ارتكاب پول ‌شويي يا تأمين مالي تروريسم توسط ارباب ‌رجوع و نگهداري عوايد حاصل از آن در صندوق امانات وجود داشته باشد، شخص مشمول مكلف است مراتب را بدون تأخير به مركز اعلام كند و از ارائه خدمت مزبور به ارباب‌ رجوع تا زمان وصول دستور مركز حداكثر تا (24) ساعت، ممانعت به ‌عمل آورد.
                </p>
                <p class="mx-auto">
                    ماده 69 - متصديان شناسايي ارباب ‌رجوع مكلف‌ اند در مواردي كه نسبت به اصالت يا صحت مدارك شناسايي ارائه ‌شده توسط ارباب ‌رجوع (اعم از حقيقي و حقوقي) ابهام داشته باشند، از طريق تحقيق از ساير نظام ‌ها و پايگاه ‌هاي اطلاعاتي و يا استعلام از مراجع ذي ‌ربط قانوني مطلع، نسبت به رفع ابهام اقدام كنند. ارائهٴ خدمت تا رفع ابهام متوقف مي ‌گردد.
                </p>
                <p class="mx-auto">
                    تبصره - چنانچه در ‌هر يك از مراحل انجام رويه ‌هاي شناسايي (ساده، معمول و مضاعف) مشخص شود ارباب‌ رجوعي به ارائهٴ اطلاعات فاقد صحت يا اصالت مبادرت كرده است، اشخاص مشمول مكلف ‌اند مطابق با مقررات نسبت به ارسال گزارش موارد مشكوك براي مركز اقدام كنند.
                </p>
                <p class="mx-auto">
                    ماده 70 - ارائه هرگونه خدمات كه كاربرد آن‌ ها صرفاً در فعاليت ‌هاي تجاري توجيه دارد و براي شخص تعهدآور است (نظير گشايش اعتبار اسنادي، صدور هرگونه ضمانت‌ نامه، اعطاي هرگونه ابزار پذيرش و ارائهٴ دسته ‌چك) به محجورين ممنوع است. ارائهٴ ساير خدمات به محجورين در چهارچوب مقررات كشور و منوط به رعايت مقررات مبارزه با پول ‌شويي و تأمين مالي تروريسم و خطرپذيري (ريسك‌) هاي مترتبه بلامانع است.
                </p>
                <p class="mx-auto">
                    ماده 71 - مؤسسات مالي و اعتباري مي‌ توانند در دستورالعمل‌ هاي داخلي خود علاوه بر مدارك ذكر شده در اين آيين ‌نامه، مدارك تكميلي را كه به شناسايي دقيق ‌تر مشتري كمك مي‌ كند، مطالبه كنند.
                </p>
                <p class="mx-auto">
                    ماده 72 - ارائه ‌دهندگان خدمات پرداخت مكلف‌ اند از اعطاي هرگونه ابزار پذيرش اعم از فيزيكي و مجازي به اشخاصي كه بر اساس استعلام از پايگاه يكپارچهٴ اطلاعات شغلي موضوع سامانهٴ مادهٴ (۲۶) اين آيين ‌نامه‌ فاقد مجوز شغلي و يا بر اساس استعلام از پايگاه اطلاعات مالياتي فاقد پروندهٴ مالياتي ‌اند، خودداري كنند.
                </p>
                <p class="mx-auto">
                    تبصره - بانك مركزي جمهوري اسلامي ايران مكلف است ادامهٴ فعاليت كليهٴ ابزارهاي پذيرش اعطا شده بدون احراز شرايط مذكور را متوقف كند.
                </p>
                <p class="mx-auto">
                    ماده 73 - اشخاص مشمول مجازند صرفاً به اشخاص خارجي داراي شمارهٴ اختصاصي اتباع خارجي خدمت ارائه كنند. نحوهٴ ارائهٴ خدمات به اشخاص خارجي مطابق با خطر (ريسك) اين اشخاص است كه ضوابط اجرايي آن ظرف سه ماه پس از تصويب اين آيين ‌نامه توسط وزارت اطلاعات با همكاري دستگاه ‌هاي متولي نظير وزارت كشور‌، وزارت امور خارجه و بانك مركزي جمهوري اسلامي ايران در هر حوزهٴ اصلي تعيين و پس از تصويب در شورا، ابلاغ مي ‌شود.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - اشخاص مشمول مكلفند شناسايي اشخاص خارجي را بر مبناي مدارك شناسايي تعيين ‌شده توسط كارگروه موضوع تبصرهٴ (6) مادهٴ (۲۱) آيين ‌نامه و پس از استعلام از سامانهٴ موضوع مادهٴ مذكور انجام دهند.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - اشخاص حقوقي و نمايندگي ‌هاي شركت‌ هاي تجاري خارجي به ‌جز نمايندگي ‌هاي سازمان‌ هاي بين ‌المللي، سفارت‌ خانه ‌ها و اشخاص حقوقي مشابه، تنها پس از ثبت نمايندگي و اخذ شناسهٴ ملي موضوع مادهٴ (۲۰) مجاز به دريافت خدمات هستند.
                </p>
                <p class="mx-auto">
                    تبصره 3 - اعطاي شمارهٴ اختصاصي به نمايندگي‌ هاي سازمان‌ هاي بين ‌المللي، سفارتخانه‌ ها و ساير اشخاص حقوقي مشابه توسط وزارت اطلاعات، پس از تأييد وزارت امور خارجه مجاز است.
                </p>
                <p class="mx-auto">
                    ماده 74 - اشخاص مشمول مكلفند رويه ‌هاي داخلي خود را به‌ گونه ‌اي ساماندهي كنند كه فقط مديران ارشد و كاركنان صاحب صلاحيت تعيين ‌شده توسط آن ‌ها امكان ايجاد تغيير در رخ نماي (پروفايل) ارباب‌ رجوع را داشته باشند.
                </p>
                <p class="mx-auto">
                    ماده 75 - اشخاص مشمول مكلف‌ اند هنگام ارائهٴ خدمات پايه به ارباب ‌رجوع، نسبت به اخذ تعهدات لازم در موارد زير اقدام كنند:
                </p>
                <p class="mx-auto">
                    ۱ - اطلاعات مورد درخواست اشخاص مشمول را كه در اين آيين ‌نامه مشخص شده است، ارائه كنند و در اخذ و به ‌روزرساني اطلاعات موضوع قانون و آيين ‌نامه، با اشخاص مشمول همكاري‌ هاي لازم را به ‌عمل آورند.
                </p>
                <p class="mx-auto">
                    ۲ - اجازهٴ استفادهٴ ساير اشخاص را به‌ غير از خود از خدمات پايهٴ دريافتي ندهند و در صورت اطلاع از بهره ‌برداري ساير اشخاص، بلافاصله موضوع را به شخص مشمول اطلاع دهند. موارد قانوني (از جمله ولايت، وصايت، قيموميت، وكالت، و نمايندگي شخص حقوقي) به شرط درج مشخصات نماينده و انجام رويه ‌هاي شناسايي مقتضي، شامل اين بند نيست.
                </p>
                <p class="mx-auto">
                    ۳ - تعهدات لازم را در خصوص رعايت مقررات مبارزه با پول ‌شويي و تأمين مالي تروريسم و عدم بهره ‌برداري از خدمات دريافتي به ‌منظور پول ‌شويي و تأمين مالي تروريسم اخذ كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - مؤسسات مالي و اعتباري، به ‌منظور كاهش خطر (ريسك) پول‌ شويي و انجام رويه ‌هاي پايش و نظارت بر اشخاص مشمول، هنگام ارائهٴ خدمات پايه به ساير مؤسسات مالي و اعتباري، مشاغل غيرمالي و بنيادها و مؤسسات خيريه، بايد ضمن دريافت مدارك مربوط به شناسايي مقتضي (اعم از ساده، معمول و مضاعف)، تعهدات لازم را در خصوص انجام الزامات اعلامي توسط مركز و اجراي قانون و مقررات مبارزه با پول ‌شويي و تأمين مالي تروريسم از آن ‌ها اخذ كنند. در صورت خودداري اشخاص مذكور از ارائهٴ اين تعهد و يا خودداري از عمل به تعهدات، مؤسسهٴ مالي و اعتباري بايد از ارائهٴ خدمت به آن ‌ها خودداري كند.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - تعهدات فوق بايد صريح و دقيق به ارباب ‌رجوع توضيح داده شود. در صورت قبول ‌نكردن ارباب‌ رجوع و يا بي‌ توجهي ارباب ‌رجوع به تعهدات خود، ارائهٴ خدمات به وي متوقف گردد.
                </p>
                <p class="mx-auto">
                    ماده 76- اشخاص مشمول مكلفند ظرف شش ماه پس از ابلاغ آيين ‌نامه، اقدامات شناسايي مقتضي را در خصوص ارباب ‌رجوع پيشين اعمال كنند. چنانچه اشخاص مشمول قادر به اجراي اين امر نباشند،‌ لازم است از ارائهٴ خدمات پايهٴ جديد به ارباب ‌رجوع پيشين خود‌داري كنند و طبق رويه ‌هاي اعلامي از سوي دستگاه‌ هاي متولي نظارت، به ارائهٴ همهٴ خدمات پايه خاتمه دهند. همچنين، اشخاص مشمول مكلفند در خصوص ارباب ‌رجوع پيشين كه فاقد اطلاعات ضروري است، موضوع را به مركز گزارش كنند.
                </p>
                <p class="mx-auto">
                    ماده 77 - اشخاص مشمول مكلف‌ اند در مواردي كه خطر (ريسك) تعاملات كاري بالا ارزيابي مي ‌شود‌، رويه‌ ها و اقدامات شناسايي مضاعف را اتخاذ كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - مؤسسات مالي و اعتباري مكلف ‌اند حداقل در موارد زير، رويه ‌هاي شناسايي مضاعف را به اجرا گذارند:
                </p>
                <p class="mx-auto">
                    ۱ - در تعاملات كاري با اشخاص حقيقي و حقوقي (ازجمله مؤسسات مالي و اعتباري) كه مطابق اعلام مراجع بين ‌المللي خطر (ريسك) آن‌ ها بالاتر است و پس از تأييد شوراي عالي امنيت ملي؛
                </p>
                <p class="mx-auto">
                    ۲ - در مواردي كه ذي ‌نفع بيمهٴ عمر، شخص حقوقي، يا ذي ‌نفع ترتيبات حقوقي است؛
                </p>
                <p class="mx-auto">
                    ۳ - در خصوص حساب‌ هاي كارگزاري خارجي؛
                </p>
                <p class="mx-auto">
                    ۴ - ارائهٴ خدمات بانكداري اختصاصي؛
                </p>
                <p class="mx-auto">
                    ۵ - هنگام ارائهٴ خدمات پايه به اشخاص داراي خطر (ريسك) سياسي؛
                </p>
                <p class="mx-auto">
                    ۶ - در هنگام تراكنش ‌هاي غيرمعمول يا الگوهاي تراكنش غيرمعمولي كه هدف قانوني يا اقتصادي مشخصي ندارد.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - دستگاه‌ هاي متولي نظارت مكلف‌ اند با هماهنگي مركز، ظرف يك سال پس از تصويب اين آيين ‌نامه، شاخص و الگوهاي تراكنش ‌هاي ‌غيرمعمول را به مؤسسات مالي و اعتباري اعلام و آن را حداقل به‌ صورت سالانه به ‌روزرساني كنند.
                </p>
                <p class="mx-auto">
                    ماده 78 - اشخاص مشمول مكلف‌ اند در مواردي كه با در دست ‌داشتن اطلاعات و يا قراين و شواهد منطقي ظن پيدا كنند كه هويت يك فرد يا نهاد با فهرست توقيفي انطباق دارد، رويه‌ هاي شناسايي مضاعف را در مورد وي اجرا كنند. چنانچه انطباق هويت با فهرست توقيفي توسط واحد مبارزه با پول‌ شويي يا بالاترين مقام مسئول شخص مشمول تأييد شود، بايد ضوابط توقيف اموال شخص مطابق اين آيين ‌نامه اجرا شود.
                </p>
                <p class="mx-auto">
                    ماده 79 - اشخاص مشمول مكلف‌ اند رويه ‌هاي شناسايي مضاعف را به ‌گونه‌ اي اعمال كنند كه حداقل اقدامات زير صورت گيرد:
                </p>
                <p class="mx-auto">
                    ۱ - كسب اطلاعات بيشتر از مشتري مانند فعاليت اقتصادي و حجم اموال و به ‌روزرساني اطلاعات رخ نماي (پروفايل) وي اعم از اطلاعات مالك واقعي در فواصل زماني كوتاه‌ تر؛
                </p>
                <p class="mx-auto">
                    ۲ - كسب اطلاعات بيشتر در خصوص ماهيت تعامل كاري؛
                </p>
                <p class="mx-auto">
                    ۳ - كسب اطلاعات دربارهٴ منبع سرمايه يا منبع اموال ارباب‌ رجوع؛
                </p>
                <p class="mx-auto">
                    ۴ - كسب اطلاعات دربارهٴ دلايل تراكنش‌ هاي با مبالغ بالا؛
                </p>
                <p class="mx-auto">
                    ۵ - اخذ تأييديهٴ مدير ارشد براي شروع و تداوم تعامل كاري؛
                </p>
                <p class="mx-auto">
                    ۶ - استعلام از سامانه ‌هاي اطلاعاتي مورد نياز و يا بانك‌ هاي جامع اطلاعاتي؛
                </p>
                <p class="mx-auto">
                    ۷ - افزايش دقت در واپايش (كنترل) و پايش از طريق افزايش گلوگاه ‌هاي واپايشي (كنترلي)، افزايش تعداد دفعات اخذ و بررسي اطلاعات تعامل كاري و نيز تعيين و شناسايي الگوهاي تراكنش ‌هايي كه به بررسي بيشتر نياز دارند؛
                </p>
                <p class="mx-auto">
                    8 - آغاز ارائهٴ خدمت به حساب تازه افتتاح ‌شده، تنها پس از واريز از يك حساب به نام مشتري در بانكي كه استانداردهاي شناسايي معمول قابل ‌قبول دارد.
                </p>
                <p class="mx-auto">
                    ماده 80 – رويه ‌هاي شناسايي مضاعف براي ارائهٴ خدمات عبارت است از تكميل برگه (فرم) شناسايي مضاعف توسط ارباب‌ رجوع و ارائهٴ مستندات لازم براي اثبات اظهارات ارائه ‌‌شده در برگه (فرم) به اشخاص مشمول و بررسي و تأييد اظهارات و مستندات ارباب ‌رجوع توسط واحد مبارزه با پول ‌شويي در شخص مشمول.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - در خصوص خدمات تعيين ‌شده توسط مركز، واحد مبارزه با پول ‌شويي شخص مشمول مكلف است با ارسال اظهارات و مستندات ارباب ‌رجوع، ارائهٴ خدمات را به اخذ مجوز از مركز منوط كند.
                </p>
                <p class="mx-auto">
                    تبصره ۲ – دستگاه ‌هاي متولي نظارت مكلف ‌اند ضوابط اجرايي اين ماده از جمله اقلام اطلاعاتي برگه (فرم‌) هاي مذكور را ظرف يك سال تعيين و پس از تأييد مركز ابلاغ كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۳ - اشخاص مشمول مكلفند در مواردي كه امكان انجام رويه‌ هاي شناسايي مضاعف وجود ندارد، از ارائهٴ خدمت به اين اشخاص خودداري كنند.
                </p>
                <p class="mx-auto">
                    ماده 81 - مؤسسات مالي و اعتباري و بانك مركزي جمهوري اسلامي ايران مكلف‌ اند ظرف دو سال پس از تصويب اين آيين ‌نامه، به ‌منظور ايجاد فرايند نظارت خودكار جهت شناسايي عمليات مالي يا معاملات مشكوك به پول‌ شويي و تأمين مالي تروريسم، نسبت به استقرار نرم ‌افزارهاي كشف تقلب بر روي همهٴ سامانه ‌هاي خود مبتني بر روش‌ هاي داده ‌كاوي و مطابق با استاندارد الگوي رفتار مالي اقدام كنند.
                </p>
                <p class="mx-auto">
                    ماده 82 - مؤسسات مالي و اعتباري مكلف‌ اند در هنگام ارائهٴ خدمات، نسبت به نظارت مستمر و بيشتر بر اشخاص تحت مراقبت، كه از طريق مركز به آن ‌ها اعلام مي ‌شوند، اقدام كنند.
                </p>
                <p class="mx-auto">
                    تبصره - مؤسسات مالي و اعتباري مكلف ‌اند اسامي و مشخصات اشخاص تحت مراقبت را محرمانه تلقي كنند و بلافاصله پس از دريافت اسامي و مشخصات اشخاص تحت مراقبت، نسبت به به ‌روزرساني فهرست پيشين اقدام كنند، به‌ نحوي كه فهرست اشخاص تحت مراقبت به ‌همراه آخرين اصلاحات آن همواره در اختيار كاركنان مجاز قرار داشته باشد. در صورت افشا و هرگونه استفادهٴ غيرمجاز از اطلاعات مذكور، با متخلفان برابر قانون رفتار خواهد شد.
                </p>
                <p class="mx-auto">
                    ماده 83 ‌- مركز مكلف است ضوابط، معيارها و رويه‌ هاي تشخيص اشخاص مظنون را تدوين كند و به ‌تأييد شورا برساند.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - مركز مكلف است تغييرات فهرست اشخاص مظنون را در حداقل زمان ممكن به اطلاع اشخاص مشمول برساند.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - اشخاص مشمول مكلف ‌اند اطلاعات و مستندات مورد نياز را در قالب و مدت ‌زمان تعيين ‌شده در اختيار مركز قرار دهند و امكان ارائهٴ مستندات را براي اشخاصي كه در ‌فرايند بررسي قرار دارند، فراهم كنند.
                </p>
                <p class="mx-auto">
                    ماده 83 - اشخاصي كه اسامي آن ‌ها در فهرست اشخاص مظنون قرار دارد، مي‌ توانند با مراجعه به مؤسسات مالي و اعتباري و ارائهٴ مستندات لازم، درخواست خود مبني بر خروج از اين فهرست را ارائه كنند. مركز پس از بررسي و تأييد اين مستندات، مكلف است نسبت به خارج ‌كردن نام شخص از فهرست اشخاص مظنون و اعلام آن به مؤسسات مالي و اعتباري اقدام كند.
                </p>
                <p class="mx-auto">
                    ماده 85 - مؤسسات مالي و اعتباري مكلف‌ اند پس از اعلام فهرست اشخاص مظنون، فوراً از ارائهٴ خدمات پايهٴ جديد به اين اشخاص خودداري و محدوديت‌ هاي اعلام ‌شده توسط مركز را در خصوص آن‌ ها اعمال كنند.
                </p>
                <p class="mx-auto">
                    تبصره - محدوديت‌ هاي ايجاد شده از تاريخ ورود شخص به فهرست اشخاص مظنون بايد اعمال شود و تنها در صورت تأييد مستندات ارائه ‌شده از سوي شخص مظنون به مركز، از فهرست مذكور خارج مي ‌شود. اين اشخاص به مدت يك سال در فهرست اشخاص تحت مراقبت قرار خواهند گرفت.
                </p>
                <p class="mx-auto">
                    ماده 86 - اشخاص مشمول مكلف ‌اند اطلاعاتي كه هنگام شناسايي مضاعف ارباب ‌رجوع دريافت كرده‌ اند، در صورت اعلام مركز و به ‌نحوي كه مركز مشخص مي‌ سازد، در اختيار آن قرار دهد.
                </p>
                <p class="mx-auto">
                    ماده 87 - ‌مؤسسات مالي و اعتباري مكلف‌ اند با همكاري دستگاه ‌هاي متولي نظارت و مركز، پيش از ايجاد هرگونه شعبه يا نمايندگي در كشورهاي پرخطر از نظر ‌پول‌ شويي و ‌تأمين مالي تروريسم، تدابير مقتضي را جهت واپايش (كنترل) خطر (ريسك) اتخاذ كنند.
                </p>
                <p class="mx-auto">
                    ماده 88 – دستگاه ‌هاي متولي نظارت مكلف‌ اند پيش از ارائهٴ مجوز به مؤسسات مالي و اعتباري خارجي براي ايجاد شعبه يا نمايندگي در كشور، از مركز در خصوص وضعيت كشور اصلي از نظر مبارزه با پول ‌شويي و تأمين مالي تروريسم استعلام كنند. دستگاه ‌هاي متولي نظارت مكلف‌ اند با همكاري مركز تدابير مقتضي را در خصوص شعب يا دفاتر نمايندگي مؤسسات مالي و اعتباري كشورهاي پرخطر، جهت واپايش (كنترل) خطر (ريسك)، اتخاذ كنند.
                </p>
                <p class="mx-auto">
                    ماده 89 – دستگاه ‌هاي متولي نظارت مكلف‌ اند ضمن نظارت و پايش مستمر در خصوص روابط مؤسسات مالي و اعتباري داخل كشور با مؤسسات مالي و اعتباري كشورهاي پرخطر از نظر پول ‌شويي و تأمين مالي تروريسم، در مواردي كه خطر (ريسك) اين روابط بالا ارزيابي مي ‌شود، اقدامات مقتضي را صورت دهند و در صورت لزوم از ادامهٴ برقراري ارتباط جلوگيري كنند.
                </p>
                <p class="mx-auto">
                    ماده 90 - اشخاص مشمول مكلف‌ اند همهٴ قراردادهاي منعقده با ارباب ‌رجوع را به‌ نحوي تدوين كنند كه بر اساس آن، امكان اجراي مقررات مبارزه با پول ‌شويي و تأمين مالي تروريسم اعم از رويه‌ هاي شناسايي مضاعف، معمول و ساده‌ فراهم شود و تعهدات لازم از ارباب ‌رجوع در خصوص آن اخذ گردد.
                </p>
                <p class="mx-auto">
                    ماده 91 - همهٴ اشخاص مشمول مكلف‌ اند به‌ منظور انجام رويه‌ هاي شناسايي مقتضي در خصوص ارائهٴ خدمات غيرحضوري، مطابق با ضوابط و رويه‌ هاي شناسايي غيرحضوري عمل كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - وزارت صنعت، معدن و تجارت مكلف است با همكاري وزارت ارتباطات و فنّاوري اطلاعات و مركز، ضوابط و رويه ‌هاي شناسايي غيرحضوري ارباب ‌رجوع را در كشور متناسب با سطح خطرپذيري (ريسك) تعاملات كاري و با لحاظ ابزارها و زيرساخت‌ هاي موجود نظير كارپوشهٴ ملي ايرانيان، گواهي امضاي رقومي (ديجيتال) و تلفن همراه ملي ظرف شش ماه پس از تصويب آيين ‌نامه تدوين كند و امكان اجراي آن را براي اشخاص مشمول فراهم آورد.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - همهٴ اشخاص مشمول مكلف‌ اند همكاري‌ هاي لازم را در راستاي اجراي اين ماده با وزارت صنعت، معدن و تجارت به ‌عمل آورند.
                </p>
                <p class="mx-auto">
                    تبصره ‌3 (اصلاحي 29ˏ11ˏ1399)- ارائهٴ خدمات پايه به ارباب ‌رجوع توسط اشخاص مشمول به ‌صورت غيرحضوري با رعايت دستورالعملي كه به تصويب شورا مي رسد، خواهد بود.
                </p>
                <p class="mx-auto">
                    فصل ششم – نقل ‌و انتقال الكترونيكي وجوه و كارگزاري بانكي
                </p>
                <p class="mx-auto">
                    ماده ٴ 92 - مؤسسات مالي و اعتباري مكلفند در روابط كارگزاري بانكي برون‌ مرزي و ساير روابط مشابه، ارزيابي مناسبي از خطر (ريسك)‌ هاي پول ‌شويي و تأمين مالي تروريسم مرتبط با فعاليت ‌هاي كارگزاري بانكي انجام دهند و متعاقباً اقدامات لازم را براي شناسايي مشتري (بانك درخواست ‌كننده) به ‌‌عمل آورند. بر اين ‌اساس، بانك‌ هاي كارگزار مكلف‌ اند اطلاعات كافي را پيش از برقراري تعامل كاري و به‌ طور مستمر پس از ايجاد آن در مورد بانك‌ هاي درخواست ‌كنندهٴ روابط كارگزاري جمع‌ آوري كنند تا ماهيت فعاليت آن بانك را كاملاً بشناسند و به‌ نحو مطلوب، خطر (ريسك‌) هاي پول ‌شويي و تأمين مالي تروريسم را به‌ صورت مستمر ارزيابي كنند. حداقل عوامل يا اقداماتي كه مؤسسهٴ مالي و اعتباري بايد در خصوص بانك درخواست ‌كننده مورد نظر قرار دهد عبارتند از:
                </p>
                <p class="mx-auto">
                    1 - كشور يا حوزهٴ قضايي كه بانك درخواست‌ كننده در آن قرار دارد؛
                </p>
                <p class="mx-auto">
                    2 - جمع‌ آوري اطلاعات كافي دربارهٴ بانك درخواست‌ كنندهٴ خدمات كارگزاري با هدف شناخت كامل ماهيت كسب‌ و كار و بازار هدف آن بانك و شناخت شهرت بانك بر مبناي اطلاعات موجود در دسترس عموم؛
                </p>
                <p class="mx-auto">
                    3 - كيفيت نظارت بر بانك از جمله بررسي وجود يا نبودِ اجراي تحقيقات يا ساير اقدامات نظارتي در ارتباط با پول ‌شويي و تأمين مالي تروريسم در خصوص بانك؛
                </p>
                <p class="mx-auto">
                    4 - گروه مالي كه بانك درخواست‌ كننده به آن تعلق دارد و كشور و حوزهٴ قضايي كه شعب و واحدهاي تابع گروه در آن واقع‌ اند؛
                </p>
                <p class="mx-auto">
                    5 - اطلاعاتي در مورد مديريت و مالكيت بانك درخواست ‌كننده (به ‌ويژه وجود مالكان واقعي يا اشخاص داراي خطر (ريسك) سياسي)؛
                </p>
                <p class="mx-auto">
                    6 - هدف از خدمات ارائه ‌شده به بانك درخواست‌ كننده؛
                </p>
                <p class="mx-auto">
                    7 - شرايط و كيفيت مقررات و نظارت بانكي در كشور بانك درخواست ‌كننده (به ‌ويژه قوانين مبارزه با پول‌ شويي و تأمين مالي تروريسم)؛
                </p>
                <p class="mx-auto">
                    8 - رويه‌ ها و سياست‌ هاي پيشگيري و شناسايي پول‌ شويي و تأمين مالي تروريسم در بانك درخواست ‌كننده شامل اقدامات آن بانك براي شناسايي كافي مشتريان؛
                </p>
                <p class="mx-auto">
                    9 - امكان احراز هويت اشخاص ثالث كه مجازند از خدمات مؤسسهٴ مالي و اعتباري ‌به‌ عنوان بانك‌ كارگزار استفاده كنند؛
                </p>
                <p class="mx-auto">
                    10- ارزيابي اقدامات واپايشي (كنترلي) كه توسط بانك درخواست‌ كنندهٴ خدمات كارگزاري براي مبارزه با پول ‌شويي و تأمين مالي تروريسم انجام مي ‌شود.
                </p>
                <p class="mx-auto">
                    بصره ۱ - بانك مركزي جمهوري اسلامي ايران مكلف است حداقل ضوابط و رويه ‌هاي اجرايي اين ماده را ظرف شش ماه تدوين و ابلاغ كند.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - مؤسسات مالي و اعتباري مكلف ‌اند اطلاعات مورد نياز در مورد خط ‌مشي ‌ها و رويه‌ هاي مبارزه با پول‌ شويي و تأمين مالي تروريسم بانك كارگزار را از طريق پرسش‌ نامه‌ اي كه بانك درخواست‌ كننده تكميل كرده است يا بر اساس اطلاعات عمومي موجود ارائه ‌شده توسط وي، به ‌دست آورند و بررسي كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۳ - تصميم براي پذيرش يا ادامهٴ يك رابطهٴ كارگزاري بانكي بايد به تأييد مديريت ارشد مؤسسهٴ مالي و اعتباري برسد و مديريت ارشد بايد ‌به ‌طور منظم از روابط كارگزاري بانكي با خطرپذيري (ريسك) بالا و نحوهٴ نظارت بر آن‌ ها مطلع شود.
                </p>
                <p class="mx-auto">
                    ماده 93 - اشخاص مشمول مكلف‌ اند از برقراري هرگونه رابطهٴ كارگزاري با بانك ‌هاي پوسته ‌اي خودداري كنند.
                </p>
                <p class="mx-auto">
                    تبصره - مؤسسات مالي و اعتباري مكلف‌ اند در صورت اطلاع از رابطهٴ كارگزاري با بانك ‌هاي پوسته‌ اي‌، مراتب را به بانك مركزي اعلام نموده و مطابق رويه اعلامي توسط آن نهاد اقدام نمايند.
                </p>
                <p class="mx-auto">
                    ماده 94 - مؤسسهٴ مالي و اعتباري در ارتباط با حساب‌ هاي كارگزاري مورد استفادهٴ مستقيم اشخاص ثالث (مشتريان بانك درخواست ‌كننده) بايد اطمينان يابد بانك درخواست‌ كنندهٴ خدمات كارگزاري:
                </p>
                <p class="mx-auto">
                    1 - وظايف مربوط به شناسايي ارباب ‌رجوع را در مورد مشترياني كه دسترسي مستقيم به حساب‌ هاي بانك كارگزار دارند، اعمال مي ‌كند؛
                </p>
                <p class="mx-auto">
                    2 - قادر است در صورت درخواست مؤسسهٴ مالي و اعتباري‌، اطلاعات اخذ شده از ارباب ‌رجوع در فرايند شناسايي را در اختيار آن مؤسسهٴ مالي و اعتباري قرار دهد.
                </p>
                <p class="mx-auto">
                    ماده 95 - مؤسسات مالي و اعتباري مكلف‌ اند كليهٴ نقل ‌و انتقالات الكترونيكي ارزي برون‌ مرزي را از طريق سامانه (سيستم‌) هاي پيام ‌رسان مورد تأييد و مسير‌هاي تحت نظارت بانك مركزي جمهوري اسلامي ايران انجام دهند و هنگام ارسال كليهٴ نقل ‌و انتقالات مذكور، اطلاعات زير را دريافت كنند:
                </p>
                <p class="mx-auto">
                    1 ‏- مشخصات فرستنده شامل نام و نام خانوادگي، شمارهٴ ملي‏/ شناسهٴ ملي‏/ شمارهٴ اختصاصي اتباع خارجي، شمارهٴ شهاب، تاريخ و محل تولد، آدرس و شمارهٴ حسابي كه تراكنش از طريق آن انجام مي ‌شود؛
                </p>
                <p class="mx-auto">
                    2 ‏- مشخصات گيرنده (ذي ‌نفع) شامل نام و نام خانوادگي/ نام شركت‌، نام بانك و شمارهٴ حساب (حساب مقصد).
                </p>
                <p class="mx-auto">
                    تبصره ۱ - حواله ارسالي بايد حاوي شماره ارجاع تراكنش منحصر به فرد باشد، به ‌نحوي كه تراكنش قابلِ ‌رديابي باشد.
                </p>
                <p class="mx-auto">
                    صره ۲ - مصاديق مجاز نقل ‌و انتقال الكترونيكي برون‌‌ مرزي و اشخاص مجاز براي انجام اين دسته از عمليات به تشخيص بانك مركزي جمهوري اسلامي ايران خواهد بود.
                </p>
                <p class="mx-auto">
                    ماده 96 - مؤسسات مالي و اعتباري مكلف‌ اند كليهٴ نقل ‌و انتقالات الكترونيكي داخلي بين ‌بانكي را صرفاً از طريق زيرساخت‌ هاي ملي پرداخت بانك مركزي جمهوري اسلامي ايران از قبيل ساتنا، پايا و شتاب انجام دهند و هنگام ارسال كليهٴ اين نقل ‌و انتقالات، اطلاعات زير را در سامانه ‌هاي مربوط بانك مركزي جمهوري اسلامي ايران ثبت كنند:
                </p>
                <p class="mx-auto">
                    1 ‏- مشخصات فرستنده شامل نام و نام خانوادگي، شمارهٴ ملي‏/ شناسهٴ ملي‏/ شمارهٴ اختصاصي اتباع خارجي، شمارهٴ شهاب، آدرس و شمارهٴ حساب/ شمارهٴ شباي حسابي كه تراكنش از طريق آن انجام مي ‌شود؛
                </p>
                <p class="mx-auto">
                    2 - مشخصات گيرنده (ذي‌ نفع) شامل نام و نام خانوادگي، شمارهٴ حساب/ شمارهٴ شبا (حساب مقصد).
                </p>
                <p class="mx-auto">
                    تبصره - بانك مركزي جمهوري اسلامي ايران مكلف است ظرف يك سال پس از تصويب اين آيين‌ نامه، زيرساخت‌ هاي لازم را جهت اجرايي ‌كردن اين ماده فراهم كند.
                </p>
                <p class="mx-auto">
                    ماده 97 - مؤسسات مالي و اعتباري مكلف‌ اند هنگام انجام كليهٴ نقل ‌و انتقالات الكترونيكي درون‌ بانكي، اطلاعات زير را در سامانه‌ هاي مربوط مؤسسهٴ مالي و اعتباري ثبت كنند:
                </p>
                <p class="mx-auto">
                    1 - مشخصات فرستنده شامل نام و نام خانوادگي، شمارهٴ ملي‏/ شناسهٴ ملي‏/ شمارهٴ اختصاصي اتباع خارجي، شمارهٴ شهاب، تاريخ و محل تولد، آدرس و شمارهٴ حساب (حسابي كه تراكنش از طريق آن انجام مي ‌شود)؛
                </p>
                <p class="mx-auto">
                    2 - مشخصات گيرنده (ذي ‌نفع) شامل نام و نام خانوادگي، شمارهٴ ملي‏/ شناسهٴ ملي‏/ شمارهٴ اختصاصي اتباع خارجي، شمارهٴ شهاب، تاريخ و محل تولد، آدرس و شمارهٴ حساب (حساب مقصد).
                </p>
                <p class="mx-auto">
                    ماده 98 - مؤسسات مالي و اعتباري مكلفند چنانچه ‌به ‌عنوان واسطهٴ نقل ‌و انتقالات الكترونيكي برون ‌مرزي يا داخلي عمل مي ‌كنند، اطلاعات دريافتي مرتبط با فرستنده و گيرنده و اطلاعات ضروري تراكنش را در سر تا سر زنجيرهٴ پرداخت و در فرايند نقل ‌و انتقال و پيام ‌هاي مرتبط نگهداري كنند.
                </p>
                <p class="mx-auto">
                    تبصره - سياست ‌گذاري ‌هاي مربوط به موضوع فوق توسط بانك مركزي جمهوري اسلامي ايران تهيه مي‌ شود و به ‌تأييد شورا مي ‌رسد و بانك‌ ها مكلف‌ اند در خصوص نقل‌ و‌ انتقالات مذكور، بخشنامه ‌هاي بانك مركزي جمهوري اسلامي ايران در اين خصوص را رعايت كنند.
                </p>
                <p class="mx-auto">
                    ماده 99 - مؤسسات مالي و اعتباري مكلفند در هنگام نقل ‌و انتقالات الكترونيكي (مستقيماً يا با واسطه)، اقدامات لازم مانند نظارت در لحظه يا نظارت پس از وقوع را براي شناسايي آن دسته از نقل ‌و انتقالات الكترونيكي كه فاقد اطلاعات ضروري تراكنش از جمله اطلاعات مربوط به فرستنده يا گيرندهٴ وجه هستند، اتخاذ و همچنين در خصوص نحوهٴ عمل در مورد اين نقل ‌و انتقالات، خط ‌مشي‌ ها و رويه ‌هاي لازم مبتني بر خطر (ريسك) را تدوين كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - مؤسسات مالي و اعتباري مكلف‌ اند سامانه‌ هاي خود را به‌ گونه ‌اي طراحي كنند كه امكان شناسايي آن دسته از نقل ‌و انتقالات الكترونيكي را كه فاقد اطلاعات ضروري طرفين تراكنش است، به‌ طور خودكار فراهم و از انجام تراكنش جلوگيري كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - مؤسسات مالي و اعتباري و بانك مركزي جمهوري اسلامي ايران مكلف‌ اند با ايجاد ساز و كار لازم در سامانه‌ هاي پرداخت خود، از انجام آن دسته از نقل ‌و انتقالات الكترونيكي كه فرستنده يا دريافت ‌كنندهٴ آن يكي از اشخاص موجود در فهرست تحريمي است، به ‌صورت سامانه اي (سيستمي) جلوگيري و گزارش آن را براي مركز ارسال كنند.
                </p>
                <p class="mx-auto">
                    ماده 100 – ارائه ‌دهندگان خدمات پرداخت مكلف‌ اند نسبت به دريافت تعهدات لازم از پذيرنده مبني بر عدم استفادهٴ غيرمجاز از ابزارهاي پذيرش و نيز رعايت مقررات مبارزه با پول ‌شويي و تأمين مالي تروريسم اقدام كنند و در صورت بروز تخلف توسط آن ‌ها‌، بر اساس الزامات بانك مركزي جمهوري اسلامي ايران اقدام شود.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - مصاديق استفادهٴ غيرمجاز مانند واگذاري ابزار پذيرش به ديگران، استفاده در خارج از نقطهٴ دسترسي و استفاده از ابزار پذيرش براي ارائهٴ وجه نقد، توسط بانك مركزي جمهوري اسلامي ايران با همكاري مركز مشخص خواهد شد.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - لازم است در قرارداد ارائه ‌دهندگان خدمات پرداخت با پذيرنده تصريح گردد كه در صورت استفادهٴ غيرمجاز از ابزار پذيرش، شركت خدمات پرداخت مي‌ تواند ارائهٴ خدمات به پذيرنده را ‌به ‌طور دائم و بدون اطلاع پذيرنده متوقف كند.
                </p>
                <p class="mx-auto">
                    تبصره ۳ – در صورتي‌ كه ارباب ‌رجوع متقاضي دريافت ابزارهاي پذيرش فيزيكي يا مجازي متعدد باشد از جمله در مواردي كه يك شخص حقوقي متقاضي دريافت اين ابزارها براي دفتر مركزي، نمايندگي ‌ها و شعب خود باشد، لازم است تمام تكاليف مقرر در اين آيين ‌نامه در مورد ‌هر يك از ابزارهاي مذكور اجرا شود.
                </p>
                <p class="mx-auto">
                    تبصره ۴ - واگذاري ابزار پذيرش به اشخاص زير سن (18) سال ممنوع است.
                </p>
                <p class="mx-auto">
                    ماده 101- مؤسسات مالي و اعتباري مكلف ‌اند از صدور كارت ‌هاي هديه، كارت‌ هاي خريد بي ‌نام و كارت ‌هاي پرداخت مشابه براي ارباب ‌رجوع فاقد حساب نزد مؤسسهٴ مزبور خودداري و ارتباط بين كارت‌ هاي پرداخت مذكور را با حساب ارباب‌ رجوع در سامانه ‌هاي خود مشخص كنند.
                </p>
                <p class="mx-auto">
                    تبصره - اين كارت ‌ها مشمول ضوابط مربوط به وجه نقد است و ساير ضوابط مربوط به صدور كارت‌ هاي مذكور، به‌ منظور كاهش خطر (ريسك) پول ‌شويي و تأمين مالي تروريسم، از سوي بانك مركزي جمهوري اسلامي ايران با همكاري مركز به مؤسسات مالي و اعتباري ابلاغ خواهد شد.
                </p>
                <p class="mx-auto">
                    ماده 102 – ارائه ‌دهندگان خدمات پرداخت مكلفند پيش از ارائه هرگونه ابزار پذيرش فيزيكي، ضمن انجام شناسايي معمول ارباب ‌رجوع، اطلاعات زير را نيز از پذيرنده دريافت و پس از استعلام از مراجع ذي‌ ربط، در رخ نماي (پروفايل) ارباب ‌رجوع ثبت كنند:
                </p>
                <p class="mx-auto">
                    1 - تصوير جواز فعاليت اقتصادي مانند پروانهٴ كسب كه مجوز پذيرنده را براي كسب‌ و كار مربوط اثبات نمايد و همچنين مدارك ثبتي براي شخص حقوقي پس از تطبيق با اصل مدارك و مجوزها؛
                </p>
                <p class="mx-auto">
                    2 - نشاني كامل و شناسه (كد) پستي مطابق با مدارك گرفته‌ شده در بند (1) اين ماده كه بايد با محل نصب ابزار پذيرش يكسان باشد؛
                </p>
                <p class="mx-auto">
                    3 - شماره‌ حساب مجاز متصل به ابزار پذيرش كه بايد مشخصات صاحب آن با پذيرنده تطابق داشته باشد؛
                </p>
                <p class="mx-auto">
                    4 - ساير اطلاعات به درخواست بانك مركزي جمهوري اسلامي ايران.
                </p>
                <p class="mx-auto">
                    ماده 103 – ارائه ‌دهندگان خدمات پرداخت مكلف‌ اند پيش از ارائهٴ هرگونه ابزار پذيرش مجازي، ضمن انجام شناسايي معمول ارباب ‌رجوع، اطلاعات زير را نيز از پذيرنده دريافت و پس از استعلام از مراجع ذي ‌ربط، در رخ نماي (پروفايل) ارباب‌ رجوع ثبت كنند:
                </p>
                <p class="mx-auto">
                    1 - اطلاعات مجوز نماد اعتماد الكترونيكي پذيرنده و يا پروانهٴ كسب موضوع مادهٴ (۸۷) قانون نظام صنفي كشور - مصوب 1382 - و اصلاحات بعدي آن؛
                </p>
                <p class="mx-auto">
                    2 - نشاني كامل و شناسه (كد) پستي محل فعاليت و حضور پذيرنده (دفتر يا محلي كه تجهيزات يا سامانه‌ هاي پردازش مجازي مطابق با مجوز كسب‌ و كار يا مدارك ثبتي شخص حقوقي در آنجا مستقر است) كه بايد با محل اظهار شده در نماد اعتماد الكترونيكي يكسان باشد و توسط ارائه ‌دهندگان خدمات پرداخت مجازي به ‌صورت ميداني بررسي شود؛
                </p>
                <p class="mx-auto">
                    3 - مشخصات و نشاني اينترنتي دقيق پذيرنده و مشخصات هويتي و نشاني ميزبان پايگاه اينترنتي؛
                </p>
                <p class="mx-auto">
                    4 - شماره‌ حساب مجاز متصل به ابزار پذيرش كه بايد مشخصات صاحب آن با پذيرنده منطبق باشد؛
                </p>
                <p class="mx-auto">
                    5 - ساير اطلاعات به درخواست بانك مركزي جمهوري اسلامي ايران.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - وزارت صنعت‌، معدن‌ و تجارت مكلف است امكان استعلام سامانه اي (سيستمي) و برخط نماد اعتماد الكترونيكي را براي ارائه ‌دهندگان خدمات پرداخت و بانك مركزي جمهوري اسلامي ايران فراهم آورد.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - وزارت صنعت، معدن و تجارت مكلف است تغييرات وضعيت اعتباري نمادهاي مذكور (اعم از تعليق، انقضا و ابطال) را به ‌صورت سامانه اي (سيستمي) در اختيار بانك مركزي جمهوري اسلامي ايران قرار دهد و بانك مذكور مكلف است از ادامهٴ ارائهٴ خدمات به ابزار پذيرش مجازي جلوگيري كند.
                </p>
                <p class="mx-auto">
                    ماده 104 - ارائه‌ دهندهٴ خدمات پرداخت مكلف است پيش از نصب و راه‌ اندازي ابزار پذيرش‌، اعم از فيزيكي يا مجازي، اطلاعات زير را مطابق با اظهار پذيرنده اخذ و در سوابق داده ‌اي پذيرنده ثبت كند:
                </p>
                <p class="mx-auto">
                    ۱ - سقف انفرادي هر تراكنش‌ مالي؛
                </p>
                <p class="mx-auto">
                    ۲ - سقف گردش ماهانهٴ ‌هر يك از ابزارهاي پذيرش.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - در صورت عدم تناسب سطح فعاليت اظهار شده با اطلاعات هويتي و اقتصادي پذيرنده، ارائه‌ دهندهٴ خدمات پرداخت مكلف است سقف انفرادي تراكنش را بر اساس اطلاعات مذكور و با روشي كه به تأييد مركز مي ‌رسد، تعيين كند.
                </p>
                <p class="mx-auto">
                    تبصره ۲ – ارائه ‌دهندهٴ خدمات پرداخت مكلف است تراكنش‌ هاي بيش از سقف تعيين ‌شده براي ابزار پذيرش را به مركز مطابق ساختار اعلامي آن گزارش دهد.
                </p>
                <p class="mx-auto">
                    ماده 105- هرگونه تغيير در اطلاعات و داده‌ هاي پذيرنده، اعم از تغيير مكان فيزيكي يا مجازي، تغيير ماهيت فعاليت و نظير آن بايد در اسرع وقت توسط پذيرنده به اطلاع ارائه ‌دهندهٴ خدمات پرداخت نصب‌ كنندهٴ ابزار پذيرش برسد و شركت ارائه‌ دهندهٴ خدمات پرداخت مزبور نيز مكلف است ظرف يك هفته نسبت به به ‌روزرساني اطلاعات و داده ‌هاي مزبور در سامانه ‌هاي اطلاعاتي خود اقدام كند. اين مورد بايد به ‌روشني در قراردادهاي نصب ابزارهاي پذيرش تصريح گردد.
                </p>
                <p class="mx-auto">
                    ماده 106 - استفاده از ابزار پذيرش در هر صنفي ‌به‌ جز صنف اظهاري پذيرنده يا هر نشاني ‌غير از نقطهٴ دسترسي تعيين ‌شده در قرارداد با پذيرنده‌، بدون اخذ تأييديه از سامانهٴ جامع پذيرندگان ممنوع است و در صورت اقدام به اين امر توسط پذيرنده، ارائه ‌دهندگان خدمات پرداخت مكلف‌ اند ضمن عدم ارائهٴ خدمت به پذيرنده‌، موضوع را ‌به‌ عنوان عمليات مشكوك به مركز گزارش دهند. ارائهٴ خدمت به ابزارهاي پذيرش در خارج از مرزهاي جمهوري اسلامي ايران تنها در صورت تأييد بانك مركزي جمهوري اسلامي ايران مجاز است.
                </p>
                <p class="mx-auto">
                    تبصره ۱ – ارائه ‌دهندگان خدمات پرداخت مكلف‌ اند براي هر ابزار پذيرش، نقطهٴ دسترسي تعريف كنند تا در صورت تغيير نشاني ابزار پذيرش، بدون هماهنگي با ارائه ‌دهندگان خدمات پرداخت، امكان دريافت خدمات وجود نداشته باشد.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - نقطهٴ دسترسي براي ابزار پذيرش فيزيكي بايد به ‌گونه ‌اي باشد كه در مورد ابزار پذيرش متصل به خط تلفن و يا اتصال اينترنت ثابت، موقعيت آن متناظر با محل ثبت‌ شده براي خط تلفن و يا اتصال اينترنت ثابت باشد و در مورد ابزار پذيرش متصل به خط تلفن همراه و اتصال اينترنت همراه، مالكيت تلفن همراه و يا اتصال اينترنت، متناظر با هويت ثبت‌ شده نزد ارائه ‌دهندگان خدمات پرداخت و صاحب حساب بانكي متصل به ابزار پذيرش باشد.
                </p>
                <p class="mx-auto">
                    تبصره ۳ - ارائه‌ دهندگان خدمات پرداخت مكلف‌ اند هنگام اعطاي ابزار پذيرش فيزيكي متصل به تلفن و اتصال اينترنت همراه، محدودهٴ جغرافيايي مجاز براي فعاليت پذيرنده را در قرارداد تعيين و در صورت استفاده از اين ابزار در خارج از محدوده‌، نسبت به ارسال گزارش عمليات مشكوك براي مركز اقدام كنند. محدوده ‌ها بايد متناسب با وسعت منطقه، ميزان خطر (ريسك) پول ‌شويي و تأمين مالي تروريسم در آن و امكانات موجود تعريف شود.
                </p>
                <p class="mx-auto">
                    تبصره ۴ - نقطهٴ دسترسي براي ابزار پذيرش مجازي بايد به ‌گونه ‌اي باشد كه استفاده از آن صرفاً از طريق مشخصات و نشاني اينترنتي ثبت ‌شده نزد ارائه ‌دهندگان خدمات پرداخت و نماد اعتماد الكترونيكي امكان‌ پذير باشد.
                </p>
                <p class="mx-auto">
                    تبصره ۵ - وزارت ارتباطات و فنّاوري اطلاعات و شركت ‌هاي كارور (اپراتور) و زيرساخت مكلف‌ اند همهٴ امكانات و خدمات (سرويس‌ هاي) مورد نياز براي اجراي تكالف اين ماده را در اختيار بانك مركزي جمهوري اسلامي ايران و ارائه ‌دهندگان خدمات پرداخت قرار دهند.
                </p>
                <p class="mx-auto">
                    ماده 107 - بانك مركزي جمهوري اسلامي ايران مكلف است به ‌منظور تسهيل فرايند و نظارت بر اجراي مفاد اين فصل، سامانهٴ جامع پذيرندگان را به ‌نحوي راه‌ اندازي كند كه در دسترس مركز باشد و امكان رتبه ‌بندي شركت‌ هاي خدمات پرداخت بر اساس رويه‌ هاي مادهٴ (۴۰) و پيگرد قضايي تخلفات پذيرندگان و شركت‌ هاي خدمات پرداخت فراهم گردد.
                </p>
