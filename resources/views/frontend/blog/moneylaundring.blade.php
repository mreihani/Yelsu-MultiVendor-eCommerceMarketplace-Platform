                    فصل هفتم - خدمات مربوط به انتقال وجوه يا ارزش
                </p>
                <p class="mx-auto">
                    ماده 108 - اشتغال به خدمات مالي براي انتقال پول (ريالي يا ارزي) يا ارزش از جمله دريافت وجه، چك، حواله‌، ساير ابزارهاي پولي يا ذخيره ‌هاي ارزش و پرداخت مبلغ متناظر به هر شكل اعم از وجه نقد و به هر طريق (از قبيل مكاتبه‌، ارسال پيام، نقل ‌و انتقال يا از طريق يك شبكهٴ تسويه كه ارائه‌ دهندهٴ خدمت در آن بستر فعاليت ‌مي ‌كند)‌، منوط به اخذ مجوز لازم از دستگاه متولي نظارت مربوط است.
