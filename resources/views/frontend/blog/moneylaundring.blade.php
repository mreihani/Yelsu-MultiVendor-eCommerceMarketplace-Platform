                    31 - وجوه نقد: هرگونه مسكوك و اسكناس و انواع چك‌ هايي كه نقل‌ و انتقال آنها مستند نشده و غيرقابلِ ‌رديابي باشد؛ از قبيل چك‌ هاي عادي در وجه حامل و ساير چك‌ هايي كه دارنده آن غير از ذي ‌نفع اوليه باشد (مانند چك‌ هاي پشت ‌نويسي ‌شده توسط اشخاص ثالث، انواع چك‌ پول و چك مسافرتي) و كارت ‌هاي پرداخت بي ‌نام و موارد مشابه. وجوه نقد شامل وجوه نقد ريالي و ارزي است.
                </p>
                <p class="mx-auto">
                    32 - اموال: هر نوع دارايي، وجوه يا منابع اقتصادي اعم از مادي يا غيرمادي، محسوس يا غيرمحسوس، منقول يا غيرمنقول، نقد يا غيرنقد، مشروع يا غيرمشروع و هرنوع منفعت يا امتياز مالي يا وجوه اعم از نقد و غيرنقد و همچنين كليه اسناد قانوني مبين حق اعم از كاغذي يا الكترونيكي نظير اسناد تجاري، سهام، يا اوراق بهادار.
                </p>
                <p class="mx-auto">
                    33 - شناسايي: ‌فرايند دريافت و بررسي مستمر اطلاعات ‌ارباب ‌رجوع،‌ مرتبط با احراز هويت و ارزيابي خطر (ريسك) ‌پولشويي و ‌تأمين مالي تروريسم. سطوح شناسايي شامل سه سطح ساده، معمول و مضاعف است.
                </p>
                <p class="mx-auto">
                    34 - متمركزكننده وجوه: همه اشخاص حقوقي كه در زمينه خدمات تمركز پرداخت‌ ها از خريداران به فروشندگان، مستقيماً يا از طريق مشاركت با شركت‌ هاي خدمات پرداخت يا مؤسسات مالي و اعتباري در چهارچوب قواعد، ضوابط و مقررات كشور فعاليت كرده ‌اند يا مي‌ ‌كنند. اين اشخاص پرداخت‌ هاي بدون حضور كارت از جمله پرداخت ‌هاي درون‌ برنامه‌ اي مبتني بر زيرساخت ‌هاي همراه يا مجازي را دريافت و به سامانه ‌هاي پرداخت در نظام بانكي ارسال مي‌ كنند.
                </p>
                <p class="mx-auto">
                    35 - روابط كارگزاري: ارايه خدمات بانكي از سوي يك مؤسسه مالي و اعتباري (مؤسسه مالي و اعتباري كارگزار) به مؤسسه مالي و اعتباري ديگر (مؤسسه مالي و اعتباري درخواست ‌كننده).
                </p>
                <p class="mx-auto">
                    36 - بانك پوسته‌ اي: بانكي كه حضور فيزيكي در قلمرويي كه در آن مجوز گرفته و به ثبت رسيده است، نداشته باشد و به هيچ گروه ارايه ‌كننده خدمات مالي كه تحت نظارت مؤثر و يكپارچه ‌اي قرار دارد نيز وابسته نباشد. مديريت و بدنه اصلي اين قبيل بانك‏ ها در قلمرو قضايي (كشورها و مناطق) ديگري مستقر است. يك بانك پوسته‏ اي به ‌جز يك نمايندگي ثبت ‌شده، تشكيلات ديگري در كشوري كه در آن به ثبت رسيده است، ندارد و نماينده آن فقط محلي را براي انجام امور حقوقي بانك مزبور در آن قلمرو قضايي (كشورها و مناطق) فراهم مي‌ آورد.
                </p>
                <p class="mx-auto">
                    37 - ابزار پرداخت: انواع كارت‌ هاي فيزيكي يا مجازي و يا هر روش و ابزاري كه مؤسسات مالي و اعتباري در اختيار دارنده آن قرار مي‌ دهند و براي وي امكان دريافت، پرداخت و يا انتقال وجه را به شخص ديگر فراهم مي‌ سازند.
                </p>
                <p class="mx-auto">
                    38 - ابزار پذيرش: دستگاهي فيزيكي يا سامانه ‌اي الكترونيكي كه مي ‌توان با استفاده از ابزار پرداخت نسبت به انجام عمليات دريافت/ پرداخت و يا انتقال وجه اقدام كرد.
                </p>
                <p class="mx-auto">
                    39 - پذيرنده: شخصي كه با پذيرش كارت بانكي و با استفاده از ابزار پذيرش، نسبت به فروش كالا و يا ارايه خدمات به دارندگان كارت اقدام مي ‌كند.
                </p>
                <p class="mx-auto">
                    40 – پرداخت ‌يار: شخص حقوقي ثبت‌ شده وفق قوانين جمهوري اسلامي ايران كه در چهارچوب الزامات، ضوابط و فرايند اجرايي فعاليت پرداخت‌ ياران و پذيرندگان پشتيباني ‌شده در نظام پرداخت كشور و بر اساس قرارداد منعقده با شركت‌ هاي ارايه‌ دهنده خدمات پرداخت و توافقنامه منعقده با شركت شاپرك فعاليت مي ‌كند.
                </p>
                <p class="mx-auto">
                    41 – ارايه ‌دهندگان خدمات ارزي: همه اشخاصي كه بر اساس مجوز بانك مركزي جمهوري اسلامي ايران به ارايه خدمات مربوط به خريد و فروش، تبادل و نقل ‌و انتقالات درون‌ مرزي يا برون‌ مرزي وجوه يا ارزش ارزي مي‌ پردازند؛ نظير صرافي‌ ها و بانك ‌هاي عامل.
                </p>
                <p class="mx-auto">
                    42 - توقيف: جلوگيري موقت از نقل ‌و انتقال، تبديل، تغيير يا جابه ‌جايي اموال مشكوك به پولشويي يا تأمين مالي تروريسم يا تصرف در آنها به هر شكل بر اساس دستور صادره توسط دادگاه يا مرجع صلاحيت‌ دار.
                </p>
                <p class="mx-auto">
                    43 - انسداد: جلوگيري موقت از نقل‌ و انتقال، تبديل، تغيير يا جابه ‌جايي اموال مشكوك به پولشويي يا تأمين مالي تروريسم يا تصرف در آنها به هر شكل طبق دستور مركز به‌ مدت (24) ساعت.
                </p>
                <p class="mx-auto">
                    فصل دوم - ارزيابي خطر (ريسك) و اتخاذ رويكرد مبتني بر خطر (ريسك)
                </p>
                <p class="mx-auto">
                    ماده ۲ - شورا مكلف است ظرف سه ماه پس از تصويب اين آيين ‌نامه، به ‌منظور ارزيابي خطر (ريسك) پولشويي و تأمين مالي تروريسم در سطح ملي، نسبت به ايجاد كارگروه ملي ارزيابي خطر (ريسك)، متشكل از دستگاه ‌هاي متولي نظارت و ساير نهادهاي ذي ‌صلاح اقدام كند. وظيفه اين كارگروه هماهنگ‌ كردن فعاليت‌ ها و ساز و كارهاي مربوط به ارزيابي خطر (ريسك) در حوزه ‌هاي اصلي است.
                </p>
                <p class="mx-auto">
                    تبصره - كارگروه ملي ارزيابي خطر (ريسك) مكلف است كارگروه‌ هاي تخصصي اعم از كارگروه‌ هاي ارزيابي تهديد، آسيب ‌پذيري ملي، آسيب‌ پذيري بخش بانكداري، آسيب‌ پذيري بخش اوراق بهادار، آسيب ‌پذيري بخش بيمه، آسيب ‌پذيري ساير مؤسسات مالي و اعتباري و آسيب‌ پذيري بخش‌ هاي مشاغل ‌غيرمالي را جهت بررسي خطر (ريسك) پولشويي و تأمين مالي تروريسم تشكيل دهد. اعضاي اين كارگروه ‌ها شامل نمايندگان دستگاه ‌هاي متولي نظارت مربوط به هر حوزه، نهادهاي ذي ‌صلاح و نمايندگان اشخاص مشمول تحت نظارت اعم از بخش خصوصي يا عمومي است كه توسط كارگروه ملي ارزيابي خطر (ريسك) تعيين مي ‌گردد.
                </p>
                <p class="mx-auto">
                    ماده ۳ - كارگروه ملي ارزيابي خطر (ريسك) مكلف است ظرف يك سال پس از تشكيل، سند ملي ارزيابي خطر (ريسك) را با همكاري مركز تدوين و در دوره ‌هاي زماني سه تا پنجساله به‌ روزرساني كند.
                </p>
                <p class="mx-auto">
                    تبصره - كارگروه ملي ارزيابي خطر (ريسك) مكلف است با در نظر گرفتن شرايط كشور و بررسي آسيب ‌ها و تهديدهاي حوزه ‌هاي اصلي، نسبت به ارايه توصيه‌ هاي لازم در خصوص اعمال رويكرد مبتني بر خطر (ريسك) در مبارزه با پولشويي و تأمين مالي تروريسم و تخصيص امكانات موجود مبتني بر خطر (ريسك)‌ هاي شناسايي ‌شده اقدام و به دستگاه ‌هاي ذي‌ ربط ابلاغ كند.
                </p>
                <p class="mx-auto">
                    ماده ۴ - به‌ منظور كاهش آسيب ‌پذيري نظام مبارزه با پولشويي و تأمين مالي تروريسم كشور، مركز مكلف است ظرف شش ماه پس از تنظيم سند ملي ارزيابي خطر (ريسك)، برنامه اقدام مبتني بر سند مزبور را تدوين و آن را حداكثر در دوره ‌هاي زماني سه تا پنج ساله به ‌روزرساني كند. اين برنامه بايد شامل اقدامات دقيق و شفاف براي اشخاص مشمول و زمان ‌بندي اجراي اين اقدامات باشد. همچنين، بايد با لحاظ‌ كردن استانداردهاي بين ‌المللي، مبتني بر فضاي آتي تهديد و آسيب نظام مبارزه با پولشويي و تأمين مالي تروريسم كشور و خطر (ريسك) ‌هاي شناسايي‌ شده در حوزه‌ هاي مختلف باشد و متناسب با منابع و امكانات موجود تنظيم شود.
                </p>
                <p class="mx-auto">
                    تبصره ۱ – دستگاه ‌هاي متولي نظارت مكلفند به ‌منظور تعيين اقدامات اشخاص مشمول و ارزيابي نحوه‌ اجراي اين اقدامات توسط اين اشخاص، با مركز همكاري كنند.
                </p>
                <p class="mx-auto">
                    تبصره ۲ - مركز مكلف است ظرف شش ماه پس از نهايي‌ شدن برنامه اقدام، سامانه ‌اي را جهت مديريت و واپايش (كنترل) برنامه اقدام و نظارت بر نحوه اجراي اقدامات موضوع اين ماده توسط اشخاص مشمول تهيه، اجرا و بهره‌ برداري كند و امكان دسترسي به آن را براي دستگاه ‌هاي متولي نظارت فراهم كند.
                </p>
                <p class="mx-auto">
                    ماده ۵ - مركز مكلف است با همكاري دستگاه‌ هاي متولي نظارت نسبت به تعيين شاخص‌ ها و مستندات لازم جهت ارزيابي و تعيين ميزان پيشرفت برنامه موضوع ماده (۴) اين آيين نامه اقدام كند.
                </p>
                <p class="mx-auto">
                    ماده ۶ - مركز مكلف است سالانه گزارشي در خصوص آسيب‌ ها و تهديدهاي پولشويي و تأمين مالي تروريسم موجود در كشور و نيز روند پياده ‌سازي برنامه اقدام داخلي و چالش‌ هاي موجود در خصوص آن تهيه كند و در اختيار رؤساي قواي سه‌ گانه و نيز دفتر مقام معظم رهبري قرار دهد. اين گزارش بايد شامل پيشنهادهايي در خصوص رفع چالش‌ هاي موجود در خصوص پياده ‌سازي سند ملي ارزيابي خطر (ريسك) باشد.
                </p>
                <p class="mx-auto">
                    ماده ۷ - اشخاص مشمول مكلفند برنامه‌ هاي داخلي مبارزه با پولشويي و تأمين مالي تروريسم خود را با رويكرد مبتني بر خطر (ريسك) و همسو با سند ملي ارزيابي خطر (ريسك) و نيز برنامه اقدام موضوع ماده (۴) اين آيين ‌نامه تدوين و اجرا كنند. برنامه ‌هاي داخلي مزبور بايد در بازه زماني سه تا پنج ساله به‌ روزرساني و هر شش ماه يك‌ بار به مركز گزارش شود.
                </p>
                <p class="mx-auto">
                    ماده ۸ - به ‌منظور مديريت و كاهش خطر (ريسك)‌ هاي پولشويي و تأمين مالي تروريسم، اشخاص مشمول مكلفند پيش از ارايه هرگونه خدمت به ارباب‌ رجوع، نسبت به ارزيابي و طبقه ‌بندي خطر (ريسك) تعامل كاري اقدام و متناسب با خطر (ريسك) ارزيابي‌ شده در خصوص نحوه ارايه خدمت تصميم ‌گيري كنند. اين اشخاص بايد خطر (ريسك) ارباب‌ رجوع، منطقه و خدمت را در نظر بگيرند.
                </p>
                <p class="mx-auto">
                    تبصره 1 - طبقه‌ بندي خطر (ريسك) تعامل كاري توسط اشخاص مشمول بايد شفاف، جامع و مانع باشد؛ به‌ گونه‌ اي كه بر مبناي اين طبقه ‌بندي، ميزان اطلاعات دريافتي و نيز اقدامات صورت‌ گرفته جهت مبارزه با پولشويي و تأمين مالي تروريسم از نظر منطقي توجيه ‌پذير باشد.
                </p>
                <p class="mx-auto">
                    تبصره ۲ – دستگاه ‌هاي متولي نظارت نيز مكلفند با همكاري مركز، فهرستي از تعاملات اربابان رجوع در حوزه تحت نظارت خود تهيه و به طبقه ‌بندي خطر (ريسك) اين تعاملات از منظر پولشويي و تأمين مالي تروريسم اقدام كنند. اين فهرست كه پس از تأييد مركز به اشخاص مشمول ابلاغ مي‌ گردد، بيانگر حداقل‌ هايي است كه رعايت آن براي اشخاص مشمول الزامي است.
                </p>
                <p class="mx-auto">
                    تبصره ۳ – در صورتي‌ كه مركز خطر (ريسك) خدمت يا معامله ‌اي را بيش از سطح قابل ‌قبول تشخيص دهد، اشخاص مشمول مكلفند از ارايه آن خودداري كنند.
                </p>
                <p class="mx-auto">
                    تبصره 4 - دستگاه‌ هاي متولي نظارت مكلفند بر عملكرد اشخاص مشمول در خصوص اجراي اين ماده نظارت و گزارش‌ هاي ارزيابي خود را به مركز اعلام كنند. مركز مكلف است اين گزارش‌ ها را در رتبه ‌بندي موضوع ماده (24) لحاظ كند.
                </p>
                <p class="mx-auto">
                    ماده 9 - مركز مكلف است با همكاري نهاد‌هاي ذي‌ ربط به‌ منظور تعيين خطر (ريسك) ارباب‌ رجوع در تعاملات كاري با اشخاص مشمول، نسبت به تهيه فهرست اشخاص با خطر (ريسك) بالا اقدام كند و فهرست مزبور را با رعايت ملاحظات امنيتي و ‌به ‌طور سامانه اي (سيستمي) در اختيار اشخاص مشمول قرار دهد. به اين منظور، مركز بايد مواردي نظير شاخص‌ هاي زير را در نظر گيرد:
                </p>
                <p class="mx-auto">
                    1 - اشخاص حقوقي كه داراي شيوه فعاليت اقتصادي و ساختار مالكيت غيرمعمول يا پيچيده هستند يا اشخاصي كه فعاليت اقتصادي و مالي آنها با اهداف مقرر در اساسنامه تناسب نداشته باشد.
                </p>
                <p class="mx-auto">
                    2 - صاحبان مشاغلي كه با وجه نقد زياد سر و كار دارند.
                </p>
                <p class="mx-auto">
                    3 - اشخاص داراي خطر (ريسك) سياسي.
                </p>
                <p class="mx-auto">
                    4 - افراد پرتردد به مناطق پرخطر.
                </p>
                <p class="mx-auto">
                    5 - اشخاص داراي سابقه محكوميت پولشويي يا امنيتي.
                </p>
                <p class="mx-auto">
                    6 - اشخاص با خطر (ريسك) بالا به تشخيص ضابطان خاص مبارزه با پولشويي.
                </p>
                <p class="mx-auto">
                    تبصره ۱ - همه دستگاه‌ هاي اجرايي از قبيل وزارت كشور‌، وزارت صنعت، معدن و تجارت، وزارت امور خارجه، وزارت دادگستري، نيروي انتظامي جمهوري اسلامي ايران، گمرك جمهوري اسلامي ايران، بانك مركزي جمهوري اسلامي ايران و نهادهاي امنيتي و اطلاعاتي مكلفند امكان دسترسي مركز را به اطلاعات مورد نياز اين ماده فراهم كنند.
                </p>
                <p class="mx-auto">
                    تبصره 2 - شورا مكلف است ظرف يك سال پس از تصويب اين آيين ‌نامه‌، معيارهاي تعيين اشخاص داراي خطر (ريسك) سياسي و اطلاعات مورد نياز مركز جهت اجراي اين ماده (موضوع تبصره (1) اين ماده) را تهيه و ابلاغ كند.
                </p>
                <p class="mx-auto">
                    ماده 10 - اشخاص مشمول مكلفند در خصوص تعاملات كاري كه مركز خطر (ريسك) آن را بالا ارزيابي مي ‌كند، مطابق رويه‌ هاي اعلام‌ شده توسط مركز عمل كنند.
                </p>
                <p class="mx-auto">
                    ماده ۱۱ - مركز مكلف است نسبت به جمع‌ آوري اسامي مناطق پرخطر مطابق شاخص‌ هايي از قبيل شاخص‌ هاي زير اقدام و آنها را پس از تصويب شورا، به اشخاص مشمول ابلاغ كند:
                </p>
                <p class="mx-auto">
                    1 - مناطقي مانند گمرك‌ ها، مناطق آزاد و برخي مناطق مرزي كه از نظر پولشويي يا تأمين مالي تروريسم در معرض خطرن
                </p>
                <p class="mx-auto">
                    2 - كشورهايي كه داراي نظام ‌هاي ضد پولشويي كافي تشخيص داده نشده ‌اند.  
                </p>
